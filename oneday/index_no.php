<h2>Én dag med Linux (skjermbilder)</h2>

<p>Nå er du kanskje på det stadiet der du nesten er overbevist, og du synes kanskje at Linux ser bra nok ut til å prøves, men du er litt redd for å ta det store steget. Hva om programvaren du anvender hver dag, ikke virker på Linux? Kommer du til å bruke masse tid på å sikkerhetskopiere data, installere det, bare for å finne ut at du ikke kan bruke det fordi du ikke kan bruke funksjon X?</p> 

<p>Å kjøre en «Live CD» er til stor hjelp hvis du ikke er sikker på om du vil bruke Linux eller ikke. Men, før det, så er det kanskje greit å vise noen skjermbilder fra en typisk dag med Linux, med forskjellig programvare, bare for å vise deg hvordan det er å bruke Linux, akkurat som du går på visning før du kjøper et nytt hus. Siden du bruker en del tid foran datamaskinen din – så er det vel kanskje best å ta den riktige avgjørelsen?</p>

<p>Her er en del skjermbilder fra Ubuntu Linux. Selvfølgelig er alle fargene, bakgrunnsbildene, osv. enkle å bytte om du ønsker det! Hva du ser her, er systemstandarden.</p>

<p>Når du kjører Ubuntu Linux for første gang, vil du se dette:</p>

<img style="width:100%" src="Images/desktop.png" />

<p>Rent og pent, ikke noe rot (inntil du legger inn noe der!). Så hvor skal vi starte? «Applications»-menyen er et godt utgangspunkt hvis du vil arbeide, og her er programvaren din organisert inn i kategorier:</p>

<img style="width:100%" src="Images/applications.png" />

<p>Hvis du trenger noe som ikke er allerede installert, kan du bare starte «Ubuntu Software Center».</p>

<p>Hvis du vil bla igjennom tilgjengelige filer, går du til «Places»-menyen:</p>

<img style="width:100%" src="Images/places.png" />

<p>Hvilke applikasjoner bruker du mest? Hvis du er som de fleste, bruker du nok en god nettleser ganske så ofte. Firefox kommer preinstallert:</p>

<img style="width:100%" src="Images/firefox.png" />

<p>Men du kan på enkelt vis installere andre nettlesere, som for eksempel Chrome:</p>

<img style="width:100%" src="Images/chrome.png" />

<p>Linux-versjonene av disse nettleserne er så å si alltid de samme som deres Windows-motstykke, så det er ingen store forandringer her.</p>

<p>Mange liker å sjekke e-posten online, og med webapplikasjoner som Gmail, Yahoo Mail eller Hotmail – er alt du trenger en nettleser. Men mange liker å bruke en lokal applikasjon. Thunderbird er standard her:</p>

<img style="width:100%" src="Images/thunderbird.png" />

<p>Men mange andre applikasjoner er tilgjengelige, som Thunderbird, som er helt lik Windows-versjonen.</p>

<p>Dagligdagse applikasjoner er muligens noe du er vant med. Et godt valg er onlineversjonen Google Apps som virker perfekt for å dele og gjøre dokumenter tilgjengelige fra hvilken som helst datamaskin, men hvis du heller vil kjøre programvare lokalt, er LibreOffice et godt valg som er nesten helt likt Microsoft Office:</p>  

<img style="width:100%" src="Images/oowriter.png" />

<p>… eller hva med regneark:</p>

<img style="width:100%" src="Images/oospreadsheet.png" />

<p>… eller hvis du driver med presentasjoner:</p>

<img style="width:100%" src="Images/oopresentation.png" />

<p>Dette bør virke kjent. Den preinstallerte PDF-leseren er også enkel, rask og kraftig:</p>

<img style="width:100%" src="Images/evince.png" />

<p>Hvis du liker å chatte online, vil du nok bruke mye tid med Empathy IM (men flere er tilgjengelige):</p>

<img style="width:100%" src="Images/empathy.png" />

<p>Husk at dette er bare eksempler på kjente verktøy og applikasjoner, men mange, mange fler er tilgjengelige for hver form for oppgave. Det er enkelt å installere andre applikasjoner og de er også veldig enkle å finne.</p>

<p>Når du vil avspille media, så er den preinstallerte videoavspilleren helt perfekt uansett hvilket videoformat den leser:</p>

<img style="width:100%" src="Images/movieplayer.png" />

<p>… men VLC Media Player er også tilgjengelig og er identisk med Windows-versjonen.</p>

<p>Når det gjelder musikk, er Rhythmbox Music Player optimal:</p>

<img style="width:100%" src="Images/rhythmbox.png" />

<p>… men det er flere andre gode avspillere, som AmaroK, Banshee, osv.</p>

<p>Et godt og effektivt bildeprogram (som organiserer, sorterer, tagger, redigerer, osv.) er Shotwell:</p> 

<img style="width:100%" src="Images/shotwell.png" />

<p>Hvis du liker å lage bilder, vil du dra nytte av applikasjoner som The GIMP, som er en meget kraftig bildemanipulator (du kan prøve Windows-versjonen som er helt lik):</p>

<img style="width:100%" src="Images/gimp.png" />

<p>For vektorbasert tegning, er Inkscape er både enkel og kraftig (Windows-versjon er tilgjengelig):</p>

<img style="width:100%" src="Images/inkscape.png" />

<p>Audacity er perfekt for lydredigering og opptak (også tilgjengelig for Windows):</p>

<img style="width:100%" src="Images/audacity.png" />

<p>… og PiTiVi gjør en god jobb når det gjelder videoredigering:</p>

<img style="width:100%" src="Images/pitivi.png" />

<p>Og hvis du har en skanner så har du alt du trenger for å skanne bilder eller dokumenter:</p>

<img style="width:100%" src="Images/simplescan.png" />

<p>La oss avslutte vår skjermbildetur med noen få enkle applikasjoner som du kan dra nytte av, som en CD/DVD-brenner:</p>

<img style="width:100%" src="Images/brasero.png" />

<p>Hvis du lurer på hva som tar plass på harddisken din, så har du mulighet til å sjekke ut dette:</p>

<img style="width:100%" src="Images/diskusage.png" />

<p>… eller hva med litt spill mens sjefen ikke er tilstede ;-) :</p>

<img style="width:100%" src="Images/deminer.png" />

<p>Hvis dette ser ut som noe du kunne trives med, så kan du være sikker på at du vil like Linux. Husk at det vi har gått gjennom er en bitteliten del av hva Linux har å tilby. Lykke til!

