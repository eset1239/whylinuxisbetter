<p>অধিকাংশ মানুষ ই-মেইল চালাচালির কাজটা ব্রাউজারেই করে। কিন্তু অনেকে আছে যারা ই-মেইলের জন্য Outlook এর মত লোকাল সফটওয়্যার ব্যবহার করে। একাজের জন্য লিনাক্সে দেয়া আছে "Thunderbird", যেটার চেহারা একাজের জন্য পরিচিত অন্য সফটওয়্যারগুলোর মতইঃ</p>


