<h2>লিনাক্সের সাথে একদিন (একটি স্ক্রিনশট ভ্রমণ)</h2>

<p>লিনাক্সের পক্ষে এতগুলো পয়েন্ট দেখে এতক্ষণে আপনি হয়তো ভাবছেন, একবার চেষ্টা করে দেখতে ক্ষতি কি? কিন্তু সাথে ভয়ও আছে... প্রতিদিন যেসব কাজে আপনি অভ্যস্থ সব লিনাক্সে করা যাবে তো? এত ঝামেলা (ডেটা ব্যাকআপ, সিডি ইমেজ ডাউনলোড করে রাইট) করার পর যদি দেখা যায় একটা অতি দরকারি জিনিস লিনাক্সে নাই... তখন কি হবে? 
</p>

<p>এ প্রশ্নের জবাব পাওয়ার সবচেয়ে সঠিক আর সহজ পথটা হল <a href="\switch\try">"Live CD" চালিয়ে দেখা</a>। তারও আগে, নিত্যদিনের লিনাক্স ব্যবহারের কিছু স্ক্রিনশট দেখে নিলে মন্দ হয় না। ব্যাপারটা অনেকটা নতুন বাড়ি ভাড়া নেবার আগে বাড়ির কিছু ছবি দেখে নেয়ার মত। কম্পিউটারের সাথে আমরা প্রতিদিন একটা উল্লেখযোগ্য সময় ব্যায় করি, সেক্ষেত্রে সঠিক সিদ্ধান্তটা অবশ্যই গুরুত্বপূর্ণ।
</p>

<p>আমরা যে স্ক্রিনশট ভ্রমণটা নিতে যাচ্ছি সেটাতে লিনাক্সের "Ubuntu" ডিস্ট্রিবিউশন (ফ্লেভার) দেখানো হয়েছে। ওয়ালপেপার, বিভিন্ন ভিজুয়াল এলিমেন্টের রঙ, থিম এগুলো ইচ্ছেমত পরিবর্তন করে নেওয়া যায় (আসলে লিনাক্সের কাস্টমাইজেশনের ব্যাপ্তি উইন্ডোজ বা ম্যাক থেকে অনেকগুন বেশি)। যাইহোক, এখানে ডিফল্ট (মানে শুরুতে যেটা দেয়া থাকে) চেহারা দেখানো হয়েছে। </p>

<p>প্রথমবার লিনাক্স ডেস্কটপ অনেকটা এরকম দেখাবেঃ </p>

<img style="width:100%" src="Images/desktop.png" />

<p>পরিচ্ছন্ন এবং সিম্পল; ডেস্কটপে কোন ঝামেলা নেই (এমনকি নতুন সফটওয়্যার ইন্সটল করলেও ডেস্কটপে কোন শর্টকাট তৈরী হবে না, যতক্ষন না আপনি নিজে শর্টকাট তৈরী করছেন)। শুরু করবো কোথেকে? "Application" মেনু (উপরে ডান কোনায়) থেকে শুরু করা যেতে পারে। সব সফটওয়্যার সেখানে ক্যাটাগরি অনুযায়ী সাজানো আছে (আগেও উল্লেখ করা হয়েছে, লিনাক্সে অনেক দরকারী সফটওয়্যার শুরুতেই ইন্সটল করা থাকে। <a href="/items/all_in_one">বিস্তারিত দেখুন</a>)। </p>

<img style="width:100%" src="Images/applications.png" />

<p>যদি এমন কোন সফটওয়্যার দরকার হয় যেটা ইন্সটল করা নেই তাতেও সমস্যা নেই। "Application" মেনু থেকে "Ubuntu Software Center" (অথবা আপনি যে ফ্লেভারের (ডিস্ট্রিবিউশনের) লিনাক্স চালাচ্ছেন সেটাতে সমতুল্য সফটওয়্যার) চালু করুন।</p>

<p>যদি কম্পিউটারে থাকা ফাইল-ফোল্ডারে ঘুরোঘুরি করতে চান তাহলে "Places" মেনুতে যেতে পারেন। সেখানে প্রয়োজনীয় কিছু জায়গা (যেমন বিভিন্ন ড্রাইভ, হোম ফোল্ডার, মাই কম্পিউটার এগুলোর) শর্টকাট করা আছে (এগুলো চাইলেই পরিবর্তন করা যায়)। </p>

