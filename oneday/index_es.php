<h2>Un día con Linux (tour de imágenes)</h2>

<p>Probablemente después de todos estos argumentos ahora estás medio convencido, pensando que Linux parece lo suficientemente bueno como para al menos probarlo, pero tienes algo de miedo de hacer este gran salto. ¿Cómo podrías saber que alguna de las herramientas/aplicaciones que usas diariamente funcionará en Linux? ¿Gastarías tanta energía para respaldar tus archivos, instalar el nuevo sistema, sólo para descubrir que no puedes vivir porque te falta X característica?</p>

<p>Yo creo que probar el Live CD puede ayudarte a determinar si Linux es algo para lo que estás listo o no. De todas formas, incluso después de eso, pensé que sería bueno enseñar algunas imágenes de un típico día usando Linux, con algunos software distintos, sólo para que veas como es, permitirte pasear un poco por esta nueva casa antes de alquilártela. Ya que tú pasas algún tiempo frente a tu computadora, seguro que sabrás tomar la decisión correcta.</p>

<p>Esta imagen muestra un ejemplo del sabor Ubuntu de Linux ¡Todos los colores, fondos, temas, etc., pueden ser personalizados fácilmente! Aquí se ve la apariencia predeterminada del sistema.</p>

<p>Cuando entres a tu escritorio de Linux por primera vez, se verá algo así.</p>

<img style="width:100%" src="Images/desktop.png" />

<p>Muy limpio, muy simple, sin abarrotamiento en tu escritorio (¡hasta que le pongas tus cosas!). Entonces, ¿dónde empezar? Si quieres empezar a trabajar, las aplicaciones del menú son un buen punto de inicio, donde tus software se organizan en categorías (y, como se mencionó antes, todos los programas necesarios ya están instalados).</p>

<img style="width:100%" src="Images/applications.png" />

<p>Si necesitas algo que no está instalado, sólo tienes que cargar el "Centro de Software" (o la herramienta equivalente para el sabor de Linux que elijas) en el menú de Aplicaciones.</p>

<p>Si deseas navegar a través de los archivos, el menú Lugares te da un montón de atajos útiles (todos son, evidentemente, personalizables):</p>

<img style="width:100%" src="Images/places.png" />

<p>¿Las aplicaciones que más usas? Si eres como la mayoría de la gente, no serás capaz de funcionar adecuadamente sin un navegador web muy bueno. Firefox viene preinstalado:</p>

<img style="width:100%" src="Images/firefox.png" />

<p>Sin embargo, puedes, fácilmente, instalar otros navegadores, como Chrome:</p>

<img style="width:100%" src="Images/chrome.png" />

<p>Las versiones de Linux para estos navegadores son más o menos lo mismo que su contraparte Windows, por lo que debe serte bastante familiar.</p>

<p>A mucha gente le gusta comprobar su correo electrónico en línea, con las aplicaciones web como Gmail, Yahoo Mail o Hotmail... en ese caso todo lo que necesitas es un navegador web. Pero a muchos les gusta procesar su correo electrónico en una aplicación local. Thunderbird es el habitual aquí, y debe resultarle familiar:</p>

<img style="width:100%" src="Images/thunderbird.png" />

<p>Pero muchos otros están disponibles, tales como Thunderbird, que es exactamente lo mismo que su versión de Windows.</p>

<p>Su suite de oficina es, probablemente, un lugar donde pasar un poco de tiempo. Una buena opción es la línea de Google Apps con las herramientas que funciona muy bien para compartir y hacer los documentos disponibles desde cualquier ordenador, pero si lo prefiere para ejecutar el software en su computadora, la suite LibreOffice le proporciona las herramientas que necesita, una vez más, con un aspecto familiar, un procesador de textos:</p>

<img style="width:100%" src="Images/oowriter.png" />

<p>o un editor de hoja de cálculo:</p>

<img style="width:100%" src="Images/oospreadsheet.png" />

<p>o un editor de presentaciones:</p>

<img style="width:100%" src="Images/oopresentation.png" />

<p>Todos deben parecerle muy similares a los que ya utiliza. El visor de documentos PDF predeterminado también está bien, sencillo pero muy potente:</p>

<img style="width:100%" src="Images/evince.png" />

<p>Si te gusta chatear en línea, pasarás algún tiempo con el cliente de mensajería instantánea Empathy (pero muchos otros están disponibles):</p>

<img style="width:100%" src="Images/empathy.png" />

<p>Recuerda: éstas son sólo ejemplos de herramientas muy comunes, pero muchos, muchos otros están disponibles para cada tipo de tarea, debes sentirte muy libre para instalar otras herramientas y probarlos, ya que es tan fácil de encontrar, instalar y desinstalar desde un lugar centralizado, sin tener que buscar en la web para encontrar los instaladores para descargar.</p>

<p>Cuando se trata de reproductores multimedia, el reproductor de películas por defecto funciona a la perfección y lee casi todos los formatos de vídeo que abras en él:</p>

<img style="width:100%" src="Images/movieplayer.png" />

<p>pero VLC Media Player también está disponible y es idéntico a su versión de Windows.</p>

<p>Para la música, el reproductor de música Rhythmbox hace todo lo que necesitas:</p>

<img style="width:100%" src="Images/rhythmbox.png" />

<p>pero hay muchos otros grandes reproductores, como Amarok, Banshee, etc.</p>

<p>Una herramienta de gestión de fotos muy eficiente (administrar, clasificar, etiquetar, editar, etc) es Shotwell:</p>

<img style="width:100%" src="Images/shotwell.png" />

<p>Ahora bien, si también quieres crear contenido, disfrutarás de aplicaciones como The GIMP para manipulación de imágenes, una pieza muy potente de software (puedes probar su versión de Windows para ver si se adapta a sus necesidades):</p>

<img style="width:100%" src="Images/gimp.png" />

<p>Para la elaboración de imágenes vectoriales, Inkscape es una potente herramienta muy fácil de usar ( también disponible la versión para Windows) :</p>

<img style="width:100%" src="Images/inkscape.png" />

<p>Audacity es perfecto para la grabación de sonido multipista y edición (disponible también para Windows):</p>

<img style="width:100%" src="Images/audacity.png" />

<p>y PiTiVi hace un gran trabajo de edición de vídeo:</p>

<img style="width:100%" src="Images/pitivi.png" />

<p>Por supuesto, si posees un escáner de documentos, necesitarás ser capaz de adquirir imágenes:</p>

<img style="width:100%" src="Images/simplescan.png" />

<p>Vamos a terminar esta gira de capturas de pantalla con algunas aplicaciones sencillas que puedes encontrar útiles, tales como una grabadora de medios (CD, DVD):</p>

<img style="width:100%" src="Images/brasero.png" />

<p>Un analizador de uso del disco, para ver qué ficheros ocupan la mayor cantidad de espacio en el disco duro:</p>

<img style="width:100%" src="Images/diskusage.png" />

<p>o un poco de juego de las minas cuando su jefe no está mirando ;-)</p>

<img style="width:100%" src="Images/deminer.png" />

<p>Si todo lo que has visto parece que te gusta para pasar tiempo con todas estas herramientas (que, una vez más, son sólo una pequeña fracción del conjunto predeterminado de herramientas, y una parte pequeñita del software disponible), entonces ¡parece que debes continuar con el siguiente paso y probar Linux de verdad!</p>

