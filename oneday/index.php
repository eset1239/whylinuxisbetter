<h2>One day with Linux (a screenshot tour)</h2>

<p>Now you're probably at the point where you're half-convinced by all these arguments, thinking that Linux seems to be a pretty neat thing to at least try, but you're kind of afraid to make the big jump. How do you know one of the tools/applications you use every day won't be lacking with Linux? Will you spend all that energy backing up your data, installing a new system, only to find that you really can't live with it because it's missing feature X?
</p>

<p>I do believe trying out a Live CD should help you determine whether Linux is really made for you or whether you're not ready yet. However, even before that, I thought it would be good to show you a few screenshots of a typical day using Linux, with a few different pieces of sofware, just to show you how it's like, let you visit this new house for a couple of hours before you decide to rent it. After all, you spend quite a bit of time with your computer everyday, you might as well make the right decision!
</p>

<p>This is a screenshot tour taking the example of the Ubuntu Linux flavor. Of course all the colors, wallpapers, themes, etc., are very easily customizable! What you will see here is merely the system default.</p>

<p>When you enter your Linux desktop for the first time, you will see something like this:</p>

<img style="width:100%" src="Images/desktop.png" />

<p>Very clean, very simple, no clutter on your Desktop (until you put your stuff in there!). So where to start? The Applications menu is a good starting point if you want to start working, where your software is neatly organized into categories (and, as we mentioned earlier, everything you're likely to use right away is already installed):</p>

<img style="width:100%" src="Images/applications.png" />

<p>If you need something that's not installed yet, just load the "Ubuntu Software Center" (or the equivalent tool for the Linux flavor you'll choose) for the Applications menu.</p>

<p>If you want to browse through your available files, the Places menu gives you plenty of handy shortcuts (all are obviously customizable):</p>

<img style="width:100%" src="Images/places.png" />

<p>Which applications do you use the most? If you're like most people, you won't be able to work properly without a very good web browser. Firefox comes preinstalled:</p>

<img style="width:100%" src="Images/firefox.png" />

<p>But you can easily install other browsers, such as Chrome:</p>

<img style="width:100%" src="Images/chrome.png" />

<p>The Linux versions for these browsers are pretty much the same as their Windows counterpart, so you should feel pretty familiar there.</p>

<p>Many people like to check their email online, with web applications like Gmail, Yahoo Mail or Hotmail -- in that case all you need is a web browser. But many like to process their email in a local application. Thunderbird is the default here, and should look familiar:</p>

<img style="width:100%" src="Images/thunderbird.png" />

<p>But many others are available, such as Evolution.</p>

<p>Your usual office-like suite of application is probably a place where you spend quite a bit of time. A good choice is the online Google Apps tools which works great for sharing and making your documents available from any computer, but if you prefer to run the software on your computer, the LibreOffice suite provides you with tools that should, here again, look familiar, like a word processor:</p>

<img style="width:100%" src="Images/oowriter.png" />

<p>or a spreadsheet editor:</p>

<img style="width:100%" src="Images/oospreadsheet.png" />

<p>or a presentation editor:</p>

<img style="width:100%" src="Images/oopresentation.png" />

<p>All of these should look very similar to what you already use. The default PDF document viewer is also nice, simple but very powerful:</p>

<img style="width:100%" src="Images/evince.png" />

<p>If you like to chat online, you'll spend some time with the Empathy instant messenger (but many others are available):</p>

<img style="width:100%" src="Images/empathy.png" />

<p>Remember, these are only examples of very common tools, but many, many others are available for each kind of task, you should feel very free to install other tools and try them out since it's so easy to find, install and uninstall them from a central place without having to search the web to find installers to download.</p>

<p>When it comes to playing media, the default movie player works flawlessly and reads pretty much every video format you throw at it:</p>

<img style="width:100%" src="Images/movieplayer.png" />

<p>but the VLC media player is also available and is identical to its Windows version.</p>

<p>For music, the Rhythmbox music player does everything you need it to:</p>

<img style="width:100%" src="Images/rhythmbox.png" />

<p>but there are many other great players, such as AmaroK, Banshee, etc.</p>

<p>A very efficient photo management tool (manage, sort, tag, edit, etc.) is ShotWell:</p>

<img style="width:100%" src="Images/shotwell.png" />

<p>Now if you also like to create content, you'll enjoy applications such as The GIMP for image manipulation, an extremely powerful piece of software (you can try its Windows version to see if it suits your needs):</p>

<img style="width:100%" src="Images/gimp.png" />

<p>For vector-based drawing, Inkscape is a very easy to use and powerful tool (Windows version available as well):</p>

<img style="width:100%" src="Images/inkscape.png" />

<p>Audacity is perfect for multi-track sound recording/editing (also available for Windows):</p>

<img style="width:100%" src="Images/audacity.png" />

<p>and Pitivi does a great job for video editing:</p>

<img style="width:100%" src="Images/pitivi.png" />

<p>Of course if you own a document scanner you'll need to be able to let it acquire some images:</p>

<img style="width:100%" src="Images/simplescan.png" />

<p>Let us finish this screenshot tour with a few simple applications that you may find handy, such as a media (CD, DVD) burner:</p>

<img style="width:100%" src="Images/brasero.png" />

<p>A neat disk usage analyzer, to see which files takes the most amount of space on your hard disk:</p>

<img style="width:100%" src="Images/diskusage.png" />

<p>or a little game of mines when your boss isn't looking ;-)</p>

<img style="width:100%" src="Images/deminer.png" />

<p>If all this looks like you would enjoy spending time with all these tools (which, once again, are only a small fraction of the default set of tools, and a teeny tiny fraction of the available software), then it sounds like you should process to the next step and try out Linux for real!</p>

