<h2>Eén dag met Linux (een screenshot-rondleiding)</h2>

<p>Nu bent u waarschijnlijk op het punt aanbeland dat u reeds half-overtuigd bent door deze argumenten, denkend dat Linux een vrij net systeem lijkt om minstens te proberen, maar u bent nogal onzeker om de grote sprong te wagen. Hoe weet u dat de gereedschappen/programma's die u elke dag gebruikt niet ontbreken op Linux? Zult u al uw energie besteden aan uw gegevens backuppen, een nieuw systeem installeren om dan te ontdekken dat u echt niet kan leven met het nieuwe systeem omdat het een bepaalde functie mist?
</p>

<p>Ik geloof dat het uitproberen van een Linux-live-cd u zou moeten helpen of Linux geschikt is voor u (en indien niet, of u er nog niet klaar voor bent). Zelfs voordat we dat doen, dacht ik dat het een goed idee was u een paar screenshots te tonen van een typische dag met Linux met enkele verschillende programma's om hoe te tonen hoe het werkt en eruitziet. Zo laat ik u kennismaken met dit nieuwe "huis", voordat u beslist het te "huren". Per slot van rekening zult u elke dag nogal wat tijd doorbrengen met uw computer everyday, dus neemt u best de juiste beslissing!
</p>

<p>Dit is een screenshot-rondleiding in de "smaak" Ubuntu Linux. Natuurlijk zijn alle kleuren, achtergronden en thema's eenvoudig te veranderen! Wat u hier ziet zijn slechts de standaardinstellingen.</p>

<p>Wanneer u de Linux-omgeving voor de eerste keer binnengaat, zal u iets zoals dit zien:</p>

<img style="width:100%" src="Images/desktop.png" />

<p>Zeer opgeruimd, eenvoudig en geen rommel op uw Bureaublad (totdat u uw spullen hierop zet!). Waar moet u beginnen? Het applicatie-menu is een goed startpunt als u wilt beginnen met werken. Hierin is de software netjes gesorteerd in categorieën (en, zoals we eerder al zeiden, alles wat u wellicht direct nodig heeft is reeds voorgeïnstalleerd):</p>

<img style="width:100%" src="Images/applications.png" />

<p>Als u iets nodig heeft dat nog niet geïnstalleerd is, open dan het "Ubuntu Software Center" (of het equivalent op een andere Linux "smaak") vanuit het Toepassingen-menu (of gebruik de Unity Dash, in recente Ubuntu-versies linksboven te vinden).</p>

<p>Als u wilt bladeren door de beschikbare bestanden, geeft het Locatie-menu u een heleboel handige links (deze zijn natuurlijk allemaal aanpasbaar):</p>

<img style="width:100%" src="Images/places.png" />

<p>Welke programma's gebruikt u het meest? Als u zoals de meeste mensen bent, zult u waarschijnlijk niet kunnen werken zonder een goede webbrowser. Firefox is voorgeïnstalleerd:</p>

<img style="width:100%" src="Images/firefox.png" />

<p>Maar u kan eenvoudig andere webbrowsers installeren, Chrome bijvoorbeeld:</p>

<img style="width:100%" src="Images/chrome.png" />

<p>De Linux-versies van deze browsers lijken sterk op hun Windows-versie, dus dit zal bekend voelen.</p>

<p>Veel mensen houden ervan hun e-mail online te controleren met webapplicaties zoals Gmail, Yahoo Mail en Outlook.com - in dit geval heeft u enkel een webbrowser nodig. Maar er zijn ook nog steeds veel mensen die hun e-mail graag in een apart programma lezen en beheren. Thunderbird is het standaardprogramma in Ubuntu. Het lijkt uiterlijk waarschijnlijk sterk op wat u reeds gebruikt:</p>

<img style="width:100%" src="Images/thunderbird.png" />

<p>Maar er zijn veel alternatieven beschikbaar, zoals Evolution en Geary.</p>

<p>Uw gebruikelijk kantoorpakket is waarschijnlijk software waar u enige tijd mee doorbrengt. Een goede keuze is het online pakket Google Apps, waarmee u eenvoudig bestanden en documenten mee kunt delen en toegang hebt vanaf eender welke computer. Als u liever een offline pakket heeft, biedt het LibreOffice-pakket gereedschappen met een uiterlijk dat u bekend is. LibreOffice bevat onder meer een tekstverwerker:</p>

<img style="width:100%" src="Images/oowriter.png" />

<p>en een spreadsheetprogramma:</p>

<img style="width:100%" src="Images/oospreadsheet.png" />

<p>en een presentatieprogramma:</p>

<img style="width:100%" src="Images/oopresentation.png" />

<p>Al deze lijken waarschijnlijk sterk op wat u reeds gebruikt. De standaard PDF-lezer is ook leuk, eenvoudig en zeer krachtig:</p>

<img style="width:100%" src="Images/evince.png" />

<p>Als u graag online chat, zult u waarschijnlijk tijd doorbrengen met Empathy (ook veel alternatieven zijn beschikbaar, zoals Pidgin):</p>

<img style="width:100%" src="Images/empathy.png" />

<p>Onthou dat dit slechts voorbeelden zijn van zeer gebruikelijke gereedschappen/programma's, maar dat er nog veel, veel meer beschikbaar zijn voor elk type taak. Voel u vrij andere programm's te installeren en deze uit te proberen - het wordt u eenvoudig gemaakt via een centrale plaats om programma's te zoeken, instaleren en te verwijderen zonder dat u het internet moet afzoeken voor installatiebestanden.</p>

<p>Wanneer het aankomt of het afspelen van mediabestanden werkt de standaard videospeler probleemloos. Deze kan ook overweg met bijna elk videoformaat dat u hem aanbiedt:</p>

<img style="width:100%" src="Images/movieplayer.png" />

<p>maar VLC media player is ook beschikbaar en identiek aan de Windows-versie.</p>

<p>Voor muziek doet de muziekspeler Rhythmbox alles wat het moet doen:</p>

<img style="width:100%" src="Images/rhythmbox.png" />

<p>maar er zijn nog veel andere, goede spelers zoals AmaroK, Banshee, enz.</p>

<p>Shotwell is een zeer efficiënte fotobeheerapplicatie (beheer, sorteer, labelen, bewerken, enz.):</p>

<img style="width:100%" src="Images/shotwell.png" />

<p>Als u graag ook inhoud creërt, zult u plezier beleven aan GIMP voor beeldbewerking, een extreem veelzijdig programma (u kan de Windows-versie proberen om te kijken of het u bevalt):</p>

<img style="width:100%" src="Images/gimp.png" />

<p>Voor vector-gebaseerd tekenen is Inkscape een zeer eenvoudige en krachtig gereedschap (Windows-versie ook beschikbaar):</p>

<img style="width:100%" src="Images/inkscape.png" />

<p>Audacity is perfect voor multi-track geluidsopname en -bewerking (ook beschikbaar voor Windows):</p>

<img style="width:100%" src="Images/audacity.png" />

<p>en Pitivi werkt geweldig voor videobewerking:</p>

<img style="width:100%" src="Images/pitivi.png" />

<p>Als u een scanner heeft, kunt u ook scannen:</p>

<img style="width:100%" src="Images/simplescan.png" />

<p>Laten we deze screenshot-rondleiding afsluiten met enkele eenvoudige programa's die van pas zouden kunnen komen, zoals een media (cd, dvd) brander:</p>

<img style="width:100%" src="Images/brasero.png" />

<p>Een keurig gereedschap om het hardeschijfgebruik te bekijken om te zien welke bestanden de meeste ruimte innemen:</p>

<img style="width:100%" src="Images/diskusage.png" />

<p>of een klein spel mijnenveger wanneer uw baas niet kijkt ;-)</p>

<img style="width:100%" src="Images/deminer.png" />

<p>Als u denkt dat u graag tijd zou doorbrengen met deze programma's (die, nogmaals, slechts een fractie zijn van de standaardprogramma's en nog een kleinere fractie van de beschikbare sofware), dan lijkt het erop dat u klaar bent voor het de volgende stap: het echt uitproberen van Linux!</p>

