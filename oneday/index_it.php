<h2>Un giorno con Linux (un tour di immagini)</h2>

<p>Ora probabilmente sei al punto di essere convinto a metà da tutte quelle argomentazioni, pensando che Linux sembri abbastanza carino almeno per provarlo, ma sei un po' spaventato a fare il grande salto. Come puoi sapere se uno di quegli strumenti/applicazioni che usi ogni giorno non mancheranno su Linux? Spenderai tutte quelle energie facendo i backup dei tuoi dati, installando un sistema nuovo, solo per scoprire che non puoi proprio viverci perchè non ha la funzione X?
</p>

<p>Credo che provare un Live CD ti possa aiutare a capire se Linux faccia veramente per te o se tu non sia ancora pronto. Tuttavia, ancora prima di quello, ho pensato che sarebbe stato utile mostrarti un po' di immagini di una tipica giornata con Linux, con qualche sofware di vario tipo, almeno per farti vedere come sembra, farti visitare questa nuova casa per un paio di ore prima che decidi di affittarla. Tutto sommato, spendi un bel po' di tempo con il tuo computer ogni giorno, è bene prendere la decisione giusta!
</p>

<p>Questo è un tour di immagini che prende come esempio la varietà Ubuntu Linux. Ovviamente tutti i colori, sfondi, temi, ecc., sono facilmente modificabili! Quello che vedrai qui è semplicemente il sistema predefinito.</p>

<p>Quando accedi al tuo desktop Linux per la prima volta, vedrai qualcosa come questo:</p>

<img style="width:100%" src="Images/desktop.png" />

<p>Molto pulito, molto semplice, niente confusione sul tuo Desktop (a meno che non metti la tua roba lì!). Allora da dove partire? Il menu Applicazioni è un buon punto d'inizio se vuoi cominciare a lavorare, dove il software è ben organizzato in categorie (e, come abbiamo già detto, tutto ciò che probabilmente userai è già installato):</p>

<img style="width:100%" src="Images/applications.png" />

<p>Se hai bisogno di qualcosa che non è già installato, ti basta cercare le applicazioni nell' "Ubuntu Software Center" (o lo strumento equivalente per la varietà Linux che sceglierai).</p>

<p>Se vuoi cercare tra i vari file disponibili nel computer, il menu Risorse ti offre tutte le scorciatoie necessarie (ovviamente sono modificabili):</p>

<img style="width:100%" src="Images/places.png" />

<p>Quali applicazioni usi più spesso? Se sei come la maggior parte della gente, non riuscirai a lavorare nel modo migliore senza un ottimo browser web. Firefox è preinstallato:</p>

<img style="width:100%" src="Images/firefox.png" />

<p>Puoi comunque installare altri browser facilmente, come Chrome:</p>

<img style="width:100%" src="Images/chrome.png" />

<p>Le versioni Linux di questi browser sono praticamente identiche delle controparti Windows, quindi dovresti trovarti a tuo agio.</p>

<p>Molte persone si trovano bene a controllare le loro email online, con applicazioni web come Gmail, Yahoo Mail o Hotmail -- in quel caso ti basta avere un browser web. Ma altri preferiscono organizzare le loro email in un'applicazione locale. Thunderbird è il programma predefinito e dovrebbe esserti familiare, in quanto è esattamente lo stesso della sua versione Windows:</p>

<img style="width:100%" src="Images/thunderbird.png" />

<p>Ma molti altri sono disponibili, come Evolution, scaricabile dall'Ubuntu Software Center.</p>

<p>La tua suite di applicazioni simil-office abituale è probabilmente uno strumento con cui trascorri molto tempo. Una buona scelta sono gli strumenti online di Google Apps che funzionano alla grande per la condivisione e rendere i tuoi documenti disponibili da qualsiasi computer, ma se preferisci avviare il software dal tuo computer, la suite LibreOffice (o recentemente LibreOffice) si presenta con strumenti che, di nuovo, dovrebbero sembrarti familiari, come un gestore di testi:</p>

<img style="width:100%" src="Images/oowriter.png" />

<p>o un editor di fogli di calcolo:</p>

<img style="width:100%" src="Images/oospreadsheet.png" />

<p>o un editor di presentazioni:</p>

<img style="width:100%" src="Images/oopresentation.png" />

<p>Tutti questi dovrebbero sembrare molto simili a ciò che già usi. Anche il visualizzatore di PDF predefinito è ottimo, semplice ma molto potente:</p>

<img style="width:100%" src="Images/evince.png" />

<p>Se ti piace chattare online, trascorrerai molto tempo con il programma di messaggistica istantanea Empathy (ma molti altri sono disponibili):</p>

<img style="width:100%" src="Images/empathy.png" />

<p>Ricorda, questi sono solo esempi di strumenti comuni, ma molti, molti altri sono disponibili per ogni tipo di utilizzo, ti sentirai assolutamente libero di installare e provare altri programmi data la semplicità con cui si trovano, installano e disinstallano da un unico punto centrale senza il bisogno di navigare nel web in cerca di file di installazione da scaricare.</p>

<p>Se parliamo di eseguire file multimediali, il riproduttore di filmati predefinito funziona perfettamente e legge praticamente tutti i formati video che gli dai in pasto:</p>

<img style="width:100%" src="Images/movieplayer.png" />

<p>ma è anche disponibile VLC media player ed è identico alla sua versione Windows.</p>

<p>Per la musica, il riproduttore musicale Rhythmbox fa tutto ciò che deve fare:</p>

<img style="width:100%" src="Images/rhythmbox.png" />

<p>ma ci sono anche molti altri ottimi player, come AmaroK, Banshee, ecc.</p>

<p>Uno strumento di gestione foto molto efficiente (gestire, ordinare, taggare, modificare, ecc.) è ShotWell:</p>

<img style="width:100%" src="Images/shotwell.png" />

<p>Se desideri anche creare contenuti, ti piaceranno applicazioni come The GIMP per la manipolazione di immagini, un software estremamente potente (puoi testare la sua versione Windows per vedere se soddisfa le tue necessità):</p>

<img style="width:100%" src="Images/gimp.png" />

<p>Per il disegno vettoriale, Inkscape è uno strumento molto potente e facile da usare (disponibile anche la versione Windows):</p>

<img style="width:100%" src="Images/inkscape.png" />

<p>Audacity è perfetto per registrazioni/modifiche di audio multitraccia (disponibile anche per Windows):</p>

<img style="width:100%" src="Images/audacity.png" />

<p>e Pitivi fa un ottimo lavoro per le modifiche video:</p>

<img style="width:100%" src="Images/pitivi.png" />

<p>Ovviamente se hai uno scanner vorrai poterlo usare per acquisire immagini:</p>

<img style="width:100%" src="Images/simplescan.png" />

<p>Terminiamo questo tour di immagini con alcune semplici applicazioni che potresti trovare utili, come uno strumento di masterizzazione (CD, DVD):</p>

<img style="width:100%" src="Images/brasero.png" />

<p>Un pratico analizzatore di dischi, per vedere quali file occupano la maggior parte di spazio sul tuo hard disk:</p>

<img style="width:100%" src="Images/diskusage.png" />

<p>o un piccolo gioco in stile campo minato quando il tuo capo non è nei dintorni ;-)</p>

<img style="width:100%" src="Images/deminer.png" />

<p>Se ti sembra che possa piacerti trascorrere del tempo ad usare alcuni di questi strumenti (che, diciamolo ancora, sono solo una piccola parte del set predefinito di programmi e una microscopica parte del software disponibile), allora forse dovresti fare il passo successivo e provare veramente Linux!</p>

