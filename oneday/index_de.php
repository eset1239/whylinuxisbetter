<h2>Ein Tag mit Linux (eine Screenshot-Tour)</h2>

<p>Nun bist du vielleicht an einem Punkt, wo du nur zur Hälfte von all den Argumenten überzeugt bist.
Du denkst vielleicht, dass Linux eine ganz schöne Sache ist, die man wenigstens ausprobieren sollte, aber fürchtest dich ein bisschen davor den 'großen Sprung' zu machen. 
Woher sollst du wissen, ob du nicht eins der Programme, die du jeden Tag nutzt, unter Linux vermissen wirst? Woher, dass du nicht all diese Energie umsonst aufbringst?
..dass du deine Daten sicherst und ein neues System installierst, nur um dann zu merken, dass du damit nicht leben kannst, weil es Feature X nicht bietet?</p>

<p>Ich glaube das Ausprobieren einer Live-CD sollte dir helfen herauszufinden, ob Linux wirklich für dich gemacht ist oder ob du noch nicht bereit bist. Aber noch davor wäre es gut, dir ein paar Screenshots von einem typischen Tag mit Linux mit verschiedenen Programmen zu zeigen.
So kannst du dieses 'neue Haus' für ein paar Stunden besichtigen, bevor du dich dafür entscheidest, es zu mieten. Schließlich verbringst DU jeden Tag Zeit mit deinem Computer, sodass du schon die richtige Entscheidung treffen wirst.</p>

<p>Diese Screenshot-Tour nimmt dich auf eine Reise durch die Welt von Ubuntu Linux. Natürlich sind alle Farben, Wallpapers und Themes sehr leicht anzupassen!
Was du hier sehen wirst, ist lediglich die Standardeinstellung.</p>

<p>Wenn du den Linuxdesktop das erste mal betrittst, wirst du etwas wie das hier sehen:</p>

<img style="width:100%" src="Images/desktop.png" />

<p>Sehr sauber, sehr einfach, kein Durcheinander auf deinem Desktop (außer du legst später deine Sachen dort ab). Also, wo fangen wir an? Das Applikationen-Menü, wo deine Software sauber in Kategorien geordnet ist, ist ein guter Startpunkt, wenn du mit der Arbeit anfangen willst (und, wie wir vorher schon erwähnt haben, ist alles, was du üblicherweise nutzt, bereits installiert).</p>

<img style="width:100%" src="Images/applications.png" />

<p>Wenn du etwas brauchst, das noch nicht installiert ist, wähle einfach das „Ubuntu Software Center“ (oder das entsprechende Tool der Linux-Variante, die du wählst) für neue Anwendungen.</p>

<p>Falls du deine verfügbaren Daten durchstöbern möchtest, gibt dir der Menüpunkte „Orte“ eine Fülle praktischer Verknüpfungen (alle sind einfach anpassbar):</p>

<img style="width:100%" src="Images/places.png" />

<p>Welche Anwendungen nutzt du am meisten? Wenn du wie die meisten Menschen bist, wirst du ohne einen sehr guten Webbrowser nicht vernünftig arbeiten können. Firefox ist vorinstalliert:</p>

<img style="width:100%" src="Images/firefox.png" />

<p>Aber du kannst leicht andere Browser installieren, wie etwa Chrome:</p>

<img style="width:100%" src="Images/chrome.png" />
<p>Die Linux-Versionen dieser Browser sind so ziemlich das gleiche wie ihr Windows-Gegenstück, so dass du dich schnell zurechtfinden solltest.</p>

<p>Viele Leute checken ihre E-Mail gerne online, mit Webanwendungen wie GMail, Yahoo Mail oder Hotmail – in diesem Fall ist alles, was du brauchst, der Webbrowser. Aber viele arbeiten ihre E-Mails gerne in einer lokalen Anwendung ab. Thunderbird ist hier Standard und sollte dir vertraut vorkommen:</p>

<img style="width:100%" src="Images/thunderbird.png" />

<p>Aber viele andere sind verfügbar.

<p>Die Büro-Software, die du normalerweise nutzt, ist möglicherweise etwas, mit dem du viel Zeit verbringst. Eine gute Wahl hier sind die online Google Apps Tools, die sehr gut sind, um deine Dokumente zu teilen und für alle Computer verfügbar zu machen, aber wenn du es bevorzugst, solche Software auf deinem Computer laufen zu lassen, bietet dir die LibreOffice-Suite Anwendungen, die ebenfalls bekannt aussehen sollten, wie ein Textverarbeitungsprogramm:</p>

<img style="width:100%" src="Images/oowriter.png" />

<p>oder eine Tabellenkalkulation:</p>

<img style="width:100%" src="Images/oospreadsheet.png" />

<p>oder ein Programm für Präsentationen:</p>

<img style="width:100%" src="Images/oopresentation.png" />

<p>Alle das sollte dem, was du bereits genutzt hast, sehr ähnlich sehen. Der Standard-PDF-Dokumentenbetrachter ist auch nett, einfach, aber sehr leistungsstark:</p>

<img style="width:100%" src="Images/evince.png" />

<p>Wenn du gerne online chattest, wirst du etwas Zeit mit dem Empathy Instant Messanger (Chatprogramm) verbringen (aber es sind viele andere erhältlich):</p>

<img style="width:100%" src="Images/empathy.png" />

<p>Zur Erinnerung, dies sind alles nur Beispiele für sehr verbreitete Programme, aber viele viele andere für Aufgaben aller Art sind erhältlich. Es steht dir frei, einfach andere Programme zu installieren und auszuprobieren, schon weil es so einfach ist, sie an einem zentralen Ort zu finden, zu installieren und zu deinstallieren, ohne das Netz nach Downloads und Installern durchsuchen zu müssen.</p>

<p>Wenn es darum geht, Medien abzuspielen, gibt es den Standard Movie Player, der einwandfrei arbeitet und so gut wie jedes Video Format abspielt, das man ihm vorsetzt:</p>

<img style="width:100%" src="Images/movieplayer.png" />

<p>aber der VLC Media Player ist auch erhältlich und identisch mit seiner Windowsversion.</p>

<p>Für Musik gibt es den Rhythmbox Music Player, der alles tut, wofür man ihn brauchen könnte:</p>

<img style="width:100%" src="Images/rhythmbox.png" />

<p>aber auch hier gibt es viele andere Player, wie Amarok, Banshee etc.</p>

<p>Ein sehr effizientes Foto-Management-Tool (organisieren, sortieren, taggen (kennzeichnen), bearbeiten etc.) ist Shotwell:</p>

<img style="width:100%" src="Images/shotwell.png" />

<p>Wenn du darüber hinaus gerne Inhalte erstellst, wirst du dich an Programmen wie „The GIMP“ für Bildbearbeitung erfreuen können, einer extrem leistungsfähigen Software (du kannst die Windowsversion ausprobieren, um zu sehen, ob es deinen Bedürfnissen entspricht):</p>

<img style="width:100%" src="Images/gimp.png" />

<p>Für vektorbasiertes Zeichnen ist Inkscape ein leicht zu bedienendes und mächtiges Tool (ebenfalls als Windowsversion verfügbar):</p>

<img style="width:100%" src="Images/inkscape.png" />

<p>Audacity ist perfekt für Multi-Track-Tonaufnahmen/Bearbeitung (auch für Windows erhältlich):</p>

<img style="width:100%" src="Images/audacity.png" />

<p>und Pitivi macht einen großartigen Job im Bereich der Videobearbeitung:</p>

<img style="width:100%" src="Images/pitivi.png" />

<p>Wenn du einen Scanner besitzt, musst du natürlich in der Lage sein, mit ihm Bilder einzuscannen:</p>

<img style="width:100%" src="Images/simplescan.png" />

<p>Lass uns diese Screenshot-Tour mit ein paar einfachen Applikationen beenden, die dir vielleicht praktisch erscheinen, so wie ein CD/DVD-Brennprogramm:</p>

<img style="width:100%" src="Images/brasero.png" />

<p>Ein gepflegter Festplatten-Analysierer, um zu sehen, welche Dateien den meisten Platz auf deiner Festplatte belegen:</p>

<img style="width:100%" src="Images/diskusage.png" />

<p>oder kleines Minen-Spiel, wenn dein Boss gerade nicht guckt ;-)</p>

<img style="width:100%" src="Images/deminer.png" />

<p>Wenn das alles danach aussieht, als würdest du gerne deine Zeit auf diese Tools verwenden, (welche, nochmal gesagt, nur ein Bruchteil der vorinstallierten Standard-Tools sind, und ein winzig kleiner Teil aller erhältlichen Programme), dann klingt das danach, als solltest du zum nächsten Schritt übergehen und Linux ‚in echt‘ ausprobieren!</p>

