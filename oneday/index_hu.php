<h2>Egy nap a Linuxszal (egy képernyőképtúra)</h2>

<p>Most valószínűleg azon a ponton vagy, mikor már félig meggyőztek ezek az érvek, azt gondolod, a Linux egész jó dolognak tűnik, legalábbis egy próbát megér, de még félsz egy ilyen nagy ugrástól. Honnan tudja az ember, hogy az egyik eszköz/alkalmazás, amit nap mint nap használ, nem fog hiányozni a Linuxból? Ennyi energiát pazarol arra, hogy lementse az adatait, új rendszert telepítsen, csak hogy rájöjjön, mégsem tud nélküle élni, hisz hiányzik XY funkció?</p>

<p>Én tényleg hiszek benne, hogy egy élő CD kipróbálása segíteni fog eldönteni, hogy vajon a Linux tényleg neked való-e vagy esetleg még nem állsz készen. Azonban úgy gondoltam, mindezek előtt jó lenne megmutatni neked néhány képernyőképet a Linux egy átlagos napi használatáról, néhány különböző programmal. Csak megmutatni, milyen is. Mintha meglátogatnád az új házat néhány órával azelőtt, hogy eldöntenéd, kibéreled-e. Elvégre is napjaid jó részét a számítógéped előtt töltöd, és még akár jól is járhatsz ezzel!</p>

<p>Ez a képernyőképtúra az Ubuntu Linux diztribúciót mutatja be. Természetesen a színek, hátterek, témák stb., könnyen személyre szabhatóak! Amit itt látni fogsz, az mind alapértelmezett beállítás.</p>

