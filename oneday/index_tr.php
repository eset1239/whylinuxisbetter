<p>Pek çok insan e-postalarını Gmail, Yahoo Mail veya Hotmail gibi web uygulamalarıyla çevrimiçi kontrol etmek ister; bu durumda ihtiyacınız olan tek şey bir web tarayıcı. Ancak bir çok kişi e-postalarını yerel bir istemci ile okumak ve işlemek isteyebilir. Bu amaçla Thunderbird kendiliğinden yüklü geliyor. Tanıdık görünüyor olmalı:</p>

<img style="width:100%" src="Images/thunderbird.png" />

