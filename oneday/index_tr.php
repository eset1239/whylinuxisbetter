<h2>Linux'la Bir Gün (bir ekran görüntüsü gezintisi)</h2>

<p>Muhtemelen şu anda bütün bu argümanlarla yarı ikna olmuş vaziyettesiniz, Linux'un en azından denemek için oldukça düzgün bir şey olduğunu düşünüyorsunuz, fakat bu büyük sıçramayı yapmaya çekiniyor gibisiniz. Her gün kullandığınız araçların/uygulamaların Linux'ta olup olmadığını nereden bileceksiniz? Verilerinizi yedeklemek, yeni bir sistem yüklemek için bütün o eforu sarfedecek misiniz? Üstelik belki de sonunda "x" özelliği bulunmadığından onunla yapamyacağınızı anlamak için olacak bütün bunlar...
</p>

<p>Linux'un sizin için biçilmiş kaftan olduğunu ya da henüz sizin için hazır olmadığını anlamak için bir Çalışan CD'yi kullanmanın yardımı olacağına kesinlikle inanıyorum. Bununla birlikte, bundan bile daha önce, size Linux ile geçecek tipik bir günden birkaç ekran görüntüsü göstermenin iyi olacağını düşündüm. Böylece bu evi kiralamaya karar vermeden önce onun nasıl bir şey olduğunu birkaç saatliğine görebileceksiniz. Neticede bilgisayarınızla da her gün bir hayli zaman geçiriyorsunuz, doğru bir karar vermek istersiniz!
</p>

<p>Bu Ubuntu Linux'un tadına bakmanız için hazırlanmış bir ekran görüntüsü gezintisi... Elbette, bütün renkler, duvar kağıtları, temalar vs. çok kolaylıkla kişiselleştirilebiliyor! Burada gördüğünüz sadece sistemin varsayılan durumu...</p>

<p>Linux masaüstüyle ilk defa karşılaştığınızda şöyle bir şey göreceksiniz:</p>

<img style="width:100%" src="Images/desktop.png" />

<p>Çok temiz, çok basit, masaüstünüzde herhangi bir karışıklık yok (kendinize ait şeyleri koyana kadar!). Peki nereden başlayalım? Eğer çalışmaya başlamak istiyorsanız, yazılımlarınızın kategoriler halinde tertipli bir şekilde organize edildiği Uygulamalar (Applications) menüsü iyi bir başlangıç noktası (ve daha önce söz ettiğimiz gibi, hemen kullanacağınız uygulamaların muhtemelen hepsi çoktan kurulmuş vaziyette):</p>

<img style="width:100%" src="Images/applications.png" />

<p>Henüz kurulmamış bir şeye ihtiyacınız olursa yalnızca "Ubuntu Yazılım Merkezi"ni (ya da seçtiğiniz Linux dağıtımının buna denk düşen uygulamasını) çalıştırın.</p>

<p>Eğer dosyalarınıza göz atmak isterseniz, Yerler (Places) menüsü size çok sayıda kullanışlı kısayol sunuyor (elbette hepsi kişiselleştirilebiliyor):</p>

<img style="width:100%" src="Images/places.png" />

<p>En çok hangi uygulamaları kullanırsınız? Eğer pek çok kişi gibiyseniz siz de çok iyi bir web tarayıcısı olmadan çalışamıyorsunuzdur. Firefox yüklü vaziyette geliyor:</p>

<img style="width:100%" src="Images/firefox.png" />

