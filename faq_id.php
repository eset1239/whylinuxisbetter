<h2>Tanya-jawab Ringan untuk Penggila Linux</h2>

<p>Baiklah, saya awali dengan ucapan itu, seperti yang kamu kira, 
situs ini tidak benar-benar untukmu :) melainkan untuk para pengguna 
Windows (atau Mac) yang mencoba untuk memahami Linux, seperti apa Linux, 
dan mencari apakah ada sesuatu yang berguna bagi mereka di dalamnya. 
Kamu mungkin tidak akan mendapat banyak informasi di sini tapi tidak 
buruk bila kamu meneruskan bacaannya! :-p</p>

<ul>

<li><p><b>T: Linux hampir tidak mirip sistem operasi seutuhnya seperti 
Windows, Linux hanya kernel. Mengapa para penggunanya begitu percaya?</b></p>
J: Tuan Linus, maafkan saya :) Ini hanya uraian sukarela. Jika 
kamu menjelaskan hal ini, kenyataannya memang Linux merupakan nama dari 
kernel. Perbedaan yang terlihat umumnya terletak pada perbedaan 
vendor-vendor dalam memaketkan sejumlah program pilihannya 
ke dalam sebuah Linux yang disebut Distribusi Linux. Separuh pendengar 
sudah bubar. Ini bukanlah situs yang ditujukan untuk pengguna Linux tetap 
(kecuali kalau ingin membuat situs ini menjadi lebih baik). Karena
alasan yang sama, dan atas dasar kesederhanaan, saya tidak berbicara 
tentang varian BSD dan kenyataan bahwa sebagian server-server <i>besar</i> 
memakai BSD, bukan Linux.</p> 
</li>

<li><p><b>T: Mengapa kamu yakin bahwa sistem operasi ini lebih baik daripada 
yang lain? Apakah berdasarkan pada apa-apa yang ingin dilakukan oleh 
banyak orang terhadap sistem operasi tersebut?</b></p>
<p>J: Tentu saja, saya sangat setuju. Tapi, sebelumnya, kita tuntaskan 
dulu apa yang ingin saya sampaikan: 1) Membiarkan pengguna Windows/Mac OS X 
mengetahui semuanya, 2) Memberikan beberapa alasan mengapa Linux dianggap 
sistem yang lumayan baik, dan mengapa menjadi pilihan yang tepat. 
Saya menyadari bahwa ada situs yang lebih baik daripada situs ini 
tapi apakah saya harus membuka <tt>whylinuxmaybebetterinmanycases.net</tt>?</p>

<p>Saya bukan pakar Linux ataupun seorang pemimpin sekte, dan saya tidak ingin 
menyebarkan ajaran: Saya adalah pengguna dan pengembang sejati Linux, mencoba 
untuk mempengaruhi orang-orang supaya mengenal Linux.</p>

</li>

<a name="gnu"><li><p><b>T: Sistem itu bernama GNU/Linux, bukan Linux, bodoh!</b></a></p>
<p>J: Hal tersebut semacam masalah detil dalam situs yang ditujukan untuk 
pengguna mahir Linux tapi mari kita sederhanakan. Orang mendengar "Linux", 
bukan "GNU/Linux", dan mungkin percekcokan seperti ini akan membuat 
orang "biasa" sulit untuk mengerti. Sama halnya dengan penulisan saya 
untuk "OpenOffice" daripada "OpenOffice.org", dan sebagainya.</p>

<p>Lagi pula, "T:" ini bukan pertanyaan, karena tidak mempunyai tanda tanya. 
Oh ya, bukankah saya menulis seputar tanya-jawab, alangkah bodohnya saya.</p>

</li>

<li><p><b>T: Bung, bahasa Inggrismu jelek.</b></p>
<p>J: Ya, saya menyadari hal itu dan terus memperbaikinya. Mohon bantuannya.</p>
</li>

<li><p><b>T: Sepertinya kamu berkonsentrasi pada GNOME dan Ubuntu, 
apakah tidak suka dengan KDE dan distribusi lain?</b></p> <p>J: Tentu saja tidak. 
Saya menggunakan banyak aplikasi KDE dan menyukainya (saya sangat senang 
Jika semua berusaha untuk menyatukan aplikasi-aplikasi KDE dan yang lainnya 
ke dalam GNOME). Gambar-gambar yang saya gunakan kebanyakan menampilkan 
Ubuntu karena memang distribusi itu yang digunakan dan sebagai platform 
pengembang saya. Maka dari itu, silahkan kirim gambar lainnya kepada saya. 
Saya juga memakai aplikasi AmaroK, jangan ragu-ragu untuk mengirimkan 
saran kepada saya untuk aplikasi-aplikasi KDE (dan GNOME!).</p> </li>

<li><p><b>T: Apa yang kamu ketahui tentang Linux?</b></p> 
<p>J: Sepertinya, tidak banyak yang saya ketahui! Saya hanya seorang 
anggota Ubuntu, mulai menggunakan Linux sejak tahun 1998, menulis 
beberapa buku saku (bahasa Francis) tentang perangkat lunak <i>open 
source</i> (Firefox, Linux, dll) dan berusaha melengkapinya. Saya 
membaca buku "Understand the Linux Kernel" (saat kamu membaca ini, buku 
itu telah selesai dibaca, mengerti seluk-beluk Linux dan ingin 
membuat versi sendiri, mungkin saya namai "Manux", mungkin juga tidak).</p> </li>

<a name="gaming"><li><p><b>T: Kamu mengatakan bahwa pecandu game tidak 
bisa pindah ke Linux, tapi apakah kamu tidak tahu tentang Wine atau Cedega, Steam? 
Aplikasi itu bisa menjadi andalan untuk menjalankan banyak game Windows. 
</b></p><p>J: Saya setuju dengan keberadaan perangkat lunak itu sebagai 
solusi untuk menjalankan game Windows di Linux dengan mudah. Tapi catat! 
Maksud saya, "seorang maniak game" yang membeli game-game populer dan terbaru 
dengan CPU yang di<i>overclock</i> untuk mendapatkan kinerja yang terbaik. 
Saya ragu bahwa mereka akan benar-benar pindah ke Linux.</p></li>

<a name="distros"><li><p><b>T: Tolong tambahkan distribusi linux lain di bagian penginstalan situsmu.</b></p>
<p>J: Banyak distribusi lain yang bertebaran di luar sana dan dengan keterbatasan yang ada mustahil untuk memenuhi semua orang. Seseorang yang ingin mencoba Linux sudah pasti dipusingkan 
oleh banyaknya "versi/selera" Linux. Saya memilih empat macam karena distribusi itu lumayan 
terkenal dan saya rasa, pilihan itu cukup beralasan.</p></li>

<li><p><b>T: Hei, apakah <a href="http://www.manucornet.net/pcjacking">PCJacking</a> adalah perbuatanmu?</b></p>
