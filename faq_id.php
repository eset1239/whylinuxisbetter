</li>

<li><p><b>T: Hei, apakah <a href="http://www.manucornet.net/Informatique/Gimp_Texturize.php">GIMP-Texturize</a> adalah karyamu?</b></p>
<p>J: Ya, kau tepat :) Tapi <a href="http://jb.rouquier.free.fr/">Jean-Baptiste Rouquier</a> 
