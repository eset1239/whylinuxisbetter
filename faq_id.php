</li>

<li><p><b>T: Hei, bukankah kamu adalah seseorang yang lolos <a href="http://code.google.com/summerofcode.html">Google's Summer of code</a> di mana 400 dari 9000 orang terpilih sebagai <i>hacker</i>?</b></p>
<p>J: Ya. Saya cuma beruntung: Saya mengirim 8000 proyek dan satu yang diterima. 
Saya beruntung dua kali (2005, 2006). Tahun depan, saya akan <i>open-source</i>kan <i>automatic project generator</i> saya.</p> </li>

