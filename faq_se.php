<h2>En liten FAQ för nördar</h2>

<p>Okey, jag börjar med att nämna, som du antagligen redan märkt, att hemsidan inte är till för dig :) Den är till för Windows (eller Mac)-användare som försöker lista ut vad Linux är för något, hur det ser ut, och om det kan vara någonting för dem. Du kommer antagligen inte att hitta någon vettig information här, så du kan lika gärna fortsätta att läsa Slashdot! :-p</p>

<ul>

<li><p><b>Q: Linux är knappast ett helt OS som Windows, det är ju bara kärnan. Varför låta folk tro något annat?</b></p> <p>A: Snälla förlåt mig, Saint Linus :) Detta är en frivillig förenkelse. Om man börjar med att förklara detta, att faktum är att Linux är kärnans namn, och att olika tillverkare väljer att skeppa Linux med sin egen mix av mjukvara för att göra sin alldeles egen ”Linuxdistribution”, etc., så har redan hälften somnat. Denna hemsidan är inte till för de som redan kör Linux som sitt huvud OS (förutom om man vill förbättra hemsidan). Av samma anledningar, och för enkelhetens skull, pratar jag inte om olika  BSD:er, eller det faktum att många av de <i>stora</i> servrarna kör BSD och inte Linux.</p> 
</li>

<li><p><b>Q: Varför säger du att  ett OS är bättre än ett annat? Det beror på vad man vad använda det till.</b></p>
<p>A: Självklart gör det det, jag håller fullständigt med. Men vi kan klargöra vad jag försöker att förmedla: 1) Låt Windows/Mac OS X-användare få veta att någonting annat existerar. 2) Ge ett par anledningar till varför Linux är ett riktigt grymt system, och varför det i de flesta fall är minst lika bra som något annat. Hemsidans namn är lite mer framfusigt, men skulle jag då köpt en domän med namnet
<tt>whylinuxmaybebetterinmanycases.net</tt> ?</p>

<p>Jag är ingen Linuxexpert, inte heller en sektledare, och jag vill inte heller evangelisera: Jag är bara en väldigt nöjd och glad användare/utvecklare av Linux som försöker att få fler att känna till Linux.</p>

</li>

<a name="gnu"><li><p><b>Q: Det kallas GNU/Linux, inte Linux,
tokstålle!</b></a></p>

<p>A: På en hemsida för avancerade Linuxanvändare kanske detaljer som denna gör skillnad, jag å andra sidan försöker hålla allting så simpelt som möjligt. Folk har hört talas om "Linux", inte "GNU/Linux", och det är detta typ av nördig attityd som hindrar vanliga människor från att förstå vad allt handlar om. Av samma anledning säger jag ”OpenOffice” istället för ”OpenOffice.org”, och så vidare.</p>

<p>Och förresten, ”Q:” är inte en fråga, eftersom att den inte innehåller något frågetecken. Men i alla fall, eftersom att jag skriver både frågorna och svaren.. Oh herre gud, jag är verkligen dum.</p>

</li>

<li><p><b>Q: Din engelska är fruktansvärd (om man kollar på den engelska versionen).</b></p>
<p>A: Ja, jag vet. Och jag jobbar på det. Gå vidare, tack.</p>
</li>

<li><p><b>Q: Du verkar fokusera väldigt mycket på GNOME och Ubuntu. Varför inte KDE och andra distributioner?</b></p> <p>A: Jag älskar och använder massvis av KDE-applikationer. Mina skärmavbilder visar ofta Ubuntu eftersom att det är den distributionen jag använder och utvecklar till. Du kan alltid skicka andra skärmavbildningar till mig om du känner för det. Du kanske har sett att jag har ett speciellt avsnitt för AmaroK, tveka inte att skicka in mer information om andra KDE/GNOME-program.</p> </li>

<li><p><b>Q: När allt kommer omkring, vad vet du om Linux?</b></p> 
<p>A: Antagligen inte så mycket som jag borde! Jag är medlem i Ubuntu, jag har använt Linux sedan ~1998, har skrivit några pocketböcker (på franska) om öppen mjukvara (Firefox, Linux, etc.). Men jag försöker att fixa det, det är därför jag har beställt boken ”Att förstå Linux-kärnan” (då du läser detta har jag säkerligen redan läst ut den, och skapat min egen version av Linux kallat ”Manux” – eller kanske inte).</p> </li>

<a name="gaming"><li><p><b>Q: Du sade att de som spelar spel inte borde byta till Linux, men känner du inte till Wine eller Cedega, Steam? Dessa fungerar finfint för ett växande antal Windowsspel.</b></p><p>A: Jag håller med om att flera olika program gör så att Windowswpel går att köra under Linux. Det är en funktionabel lösningen för den vardaglige spelaren. Dock passar det inte för ”hardcore-spelaren”, som köper de nyaste och fräschaste spelen så fort de kommer ut på hyllorna, och överklockar CPUn för att få bättre prestanda. Jag tvivlar på att de kan tänka sig att göra den sortens kompromiss.</p></li>

<a name="distros"><li><p><b>Q: Var snäll och lägg till Linuxdistributionen ABC till din sida om hur man installerar Linux.</b></p><p>A: Det finns många, många distributioner där ute, och den sortens limiterade offring kan inte passa alla. För människor som vill testa Linux, är det redan förvirrande med att det finns flera olika ”smaker”. Jag väljer att nämna fyra av de som är populärast, och jag tror att det är ett vettigt val.</p></li>

