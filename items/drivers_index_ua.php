<h2>Не думай про драйвери!</h2>

<p align="center">Переклад з німецької Завгородньої Тетяни</p>

<p>До нового апаратного забезпечення, навіть найпростішого, зазвичай, додається установочний диск. На диску є маленька програма, яку ще називають «драйвер». Якщо ти почитаєш інструкцію, то побачиш, що апаратне забезпечення не буде працювати під Windows без цього драйвера. Якщо ж ти такий як більшість людей, то ти не будеш читати інструкцію, а просто тестуватимеш прилад, поки не побачиш , що він без драйвера не працює.</p>

<p>Тепер встав диск, клікни на значок установки, зачекай, вийми диск та перезавантаж свій ПК.</p>

<p>Якщо ти купував прилад вже давно і хочеш і тепер використовувати його на іншому комп’ютері, ти напевне рано чи пізно десь загубиш диск, і тоді тобі прийдеться скачати нову версію зі сторінки виробника. Це може зайняти трохи часу (згадай, як дивно бувають структуровані сторінки виробника :) ).</p>

<p>О’кей, це було лише технічне забезпечення. Тепер уяви собі, що ти хочеш установити Windows на новісінький комп’ютер. Для кожного приладу ти повинен знайти новий драйвер або встановити їх з дисків, які, сподіваємось, додаються, при чому перезавантажувати комп’ютер майже після кожного драйвера. Відеокарта, звукова карта, клавіатура, миша, материнська плата з набором мікросхем і т.д. (краще почни з драйвера відеокарти, щоб працювати далі з приємними настройками екрану). І все це починається вже після довготривалої установки Windows.</p>

<p><b>Для Linux не потрібні драйвери.</b>Усі вони вже встановлені у ядрі Linux, тобто встановлюються на твій комп’ютер під час установки Linux. А це означає:</p>

<ul>
<li><b>дуже швидкий процес установки</b>. Коли він закінчений – в тебе встановлено все, що потрібно для роботи.</li>
<li><b>підтримка всіх периферійних систем:  тобі не доведеться встановлювати ніяких додаткових драйверів для миші чи клавіатури або для МР3 плейєра). .</li>
<li><b>- менше шкоди довкіллю завдяки використанню меншої кількості дисків з драйверами.</li>
</ul>

