<h2>เมามันส์กับการสนับสนุนฟรี และไม่มีจำกัด</h2>

<p>หนึ่งในสุดยอดขุมสมบัติในโลกของสังคมโอเพ่นซอร์ส (นั่นรวมถึงลินุกซ์อย่างแน่นอน) คือสังคมจริงๆ แท้ๆ ของบรรดาผู้ใช้งาน และผู้พัฒนาที่มีตัวตนจริงๆ อยู่ข้างนอก, บนเว็บบอร์ด, ในจดหมายกลุ่ม, บนไออาร์ซี (IRC) ซึ่งพร้อมช่วยเหลือผู้ใช้หน้าใหม่ พวกเขาเหล่านั้นต่างก็มีความสุขในการเห็นผู้คนทยอยกันเข้ามาเล่นกับนกเพนกวิน และพวกเขาต่างก็มีความสุขที่จะได้ช่วยให้เด็กใหม่ได้เข้าถึงระบบใหม่ของเด็กน้อยเหล่านั้น</p>

<p>ดังนั้นหากมีบางสิ่งบางอย่างที่คุณไม่เข้าใจ ไม่ว่าโปรแกรมไม่ได้เป็นไปอย่างที่คุณคาดหวัง หรือคุณสมบัติบางตัวคุณหาไม่เจอ อย่ารอช้า จงหาและร้องขอความช่วยเหลือจากพวกเขา หากแม้นว่าคุณมีคนใกล้ตัว (ครอบครัว? เพื่อนร่วมงาน?) ที่ใช้ลินุกซ์อยู่แล้ว เขาหรือเธอย่อมต้องยินดีที่จะช่วยเหลือคุณ อย่างไรก็ตาม แค่ออนไลน์คุณก็จะพบกับที่ต่างๆ มากมายที่เหล่าผู้กล้าทั้งหลายพร้อมจะตอบคำถาม และพาคุณออกจากปัญหาได้เกือบทุกครั้ง กูรูทั้งหลายเป็นคนดี หากคุณสอบถามด้วยถ้อยคำที่สุภาพ หรือแค่พิมพ์ "ลินุกซ์ ช่วยด้วย" (หรือแทนคำว่า "ลินุกซ์" ด้วยชุดแจกจ่ายที่คุณเลือกใช้ -- ดูในส่วนของการติดตั้ง) ในกูเกิ้ล และไม่ต้องสงสัยเลย คุณจะพบกับทุกสิ่งอันพึงประสงค์</p>

