<h2>Esqueça os “crapwares”</h2>

<p>Se você já comprou um computador com Windows, você sabe que isso pode ser uma experiência bem frustrante.</p>

<p>Tudo começa quando você liga pela primeira vez: milhões de janelas começam a se abrir, pedindo para que você assine um determinado serviço, adquira a versão premiu de um antivírus, jogos que você nunca jogou e nem pediu pra ter instalado, programas de escritório que pedem que você crie uma conta online, etc.</p>

<p>Mas isso não para por aí. Toda vez que você liga seu computador, esses programas pré-instalados se iniciam com o sistema, e faz que você espere cada vez mais do tempo em que você vê a área de trabalho na tela e o tempo em que o computador finalmente para de ficar lerdo por causa de todos esses programas se iniciando. </p>

<p>Além do mais, em muitos casos, quando você já tem seu computador por 30 dias, várias caixas de diálogo começam a aparecer e você percebe então que os programas que você achava que eram gratuitos (por exemplo: um antivírus) não são, e tudo o que você tem é uma versão de teste gratuito por 30 dias.</p>

<p>O problema é que muitas fabricantes de computadores pensam que esse tipo de coisa é bom. Eles instalam programas no seu computador antes de você comprar, achando que vão melhorar sua experiência, porque você vai adquirir “mais” pelo mesmo preço, e eles acreditam que isso ajuda a diferenciá-los da concorrência, que não necessariamente oferecem tais “melhorias” no sistema padrão. Para eles é um algo a mais.</p>

<p>Mas para o usuário, isso é na maioria dos casos uma dor de cabeça, maior tempo de espera, e mais desinstalação de programas inúteis, mais dinheiro gasto se você decide que precisa daquele antivírus não-tão-gratuito apesar de tudo, e, no fim de tudo, você tem um computador lento e lotado de coisas. Por isso esses programas receberam um nome nem tão amigável assim: “crapware”, (numa tradução educada do inglês: software inútil).</p>

<p>Nada disso vem com o Linux. Nenhum programa vai te encher o saco pedindo que você se inscreva em algo, ou exija que você pague ao término do período de teste, ou vai tornar seu computador cada vez mais lento e te fazer esperar cada vez mais a cada vez que você liga o computador. Linux vem com tudo o que você precisa pra trabalhar, mas não tem nenhum desses programinhas chatos, os tais <i>crapwares</i>.</p>

