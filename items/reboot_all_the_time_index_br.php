﻿<h2>Cansou de reiniciar seu computador toda hora?</h2>

<p>Acabou de atualizar uma ou duas coisas no seu sistema Windows com o "Windows Update"? Por favor reinicie. Acabou de instalar um programa novo? Por favor reinicie. Seu sistema parece instável? Tente reiniciar, provavelmente tudo funcionará melhor depois disso.</p>

<p>O Windows sempre pede para reiniciar o computador, e isso pode ser chato (talvez acontece de você estar baixando algo demorado, e você não quer interrompê-lo só porque atualizou algumas partes do sistema). Mas mesmo se clicar em "Reiniciar depois", o Windows continua te aborrecendo a cada dez minutos, para te avisar que você realmente deveria reiniciar o computador. E se acontecer de você não estar no computador, e não ver a pergunta, ele vai reiniciar automaticamente. Adeus download demorado.</p>

<p>O Linux, basicamente, não precisa de reiniciar. Se você instala um programa novo (até mesmo programas grandes), ou prepara uma atualização de rotina para seu sistema, não vão te pedir para reiniciar o computador. Isso só é necessário quando uma parte do coração do sistema tiver sido atualizada, e isso acontece uma vez depois de várias semanas.</p>

<p>Você conhece os servidores de Internet? Eles são os grandes computadores que te retornam quando você entra numa página da web, e enviam a informação à seu navegador. A maioria deles rodam Linux, já que eles precisam sempre estar disponíveis (um visitante pode vir a qualquer momento), eles não são reiniciados frequentemente (os serviços não ficam disponíveis enquanto o sistema está iniciando). Atualmente, muitos deles não têm sido reiniciados por <b>vários anos</b>. O Linux é estável, ele roda perfeitamente bem sem ser reiniciado toda hora.</p>

<p>Você provavelmente não deixará seu computador ligado por várias semanas, mas a idéia é: o sistema não te aborrecerá para ser reiniciado toda hora.</p>

