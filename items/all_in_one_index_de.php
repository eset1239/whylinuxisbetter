<h2>Wenn dein System installiert ist, warum solltest du dann <i>immer noch</i> Software nachinstallieren müssen?</h2>

<p>Windows installieren ist nur der Anfang. Stell dir vor, du hättest gerade eine brandneue Kopie von Windows 7 installiert und möchtest nun deine Fähigkeiten einsetzen. Ein Freund schickt dir eine Email mit einer angehängten PDF-Datei: Verdammt, du hast keine Programm um sie anzuzeigen. Du musst online gehen, nach der Website suchen, auf der du den Adobe Reader (oder einen anderer PDF-Reader) herunterladen kannst, ihn herunterladen, ihn installieren und dann vielleicht noch neustarten. Nun gut, alles klar, jetzt ist alles fertig. Angehängt an die Email deines Freundes findest du noch ein Textdokument, datei.doc. Dein Windows kann es genauso wenig lesen. Außer natürlich du kaufst eine Kopie von Microsoft Office oder du lädst einfach OpenOffice herunter, aber du musst es immer noch finden, herunterladen (hoffentlich hast du eine Breitbandverbindung), installieren, etc. Desweiteren hat dein Freund dir ein Foto gesendet, mit schlechtem Kontrast, Helligkeit und viel weißem Rand. Also kannst du nun Photoshop kaufen (wie viel Kohle kostet das nun wieder?), oder GIMP (ein freies Programm, mit dem du etwa gleich viele Funktionen wie mit Photoshop hast) herunterladen: suchen, herunterladen, installieren, usw. Das ist genug: In dir wächst der Verdacht, dass Windows weit von der Vollständigkeit entfernt und Installieren nur der Anfang von Schwierigkeiten ist.</p>

<p>Mit Linux (Ubuntu, Mandriva, Fedora, etc. sind verschiedene „Geschmacksrichtungen“ von Linux) bekommst du <i>ohne weitere Installation als Dreingabe</i>:</p>

<ul>
<li>Alles, was du brauchst, um Texte zu schreiben, Tabellen zu bearbeiten, schöne Präsentationen zu erstellen, zu zeichnen und Gleichungen zu verändern.</li>
<li>Ein Webbrowser (z.&#x202f;B. Firefox) und ein Emailprogramm (z.&#x202f;B. Thunderbird oder Evolution)</li>
<li>Einen Fotoeditor (GIMP), der fast so umfangreich wie Photoshop ist.</li>
<li>Ein Instant Messenger.</li>
<li>Ein Videoplayer.</li>
<li>Ein Musikplayer mit Organisationsfunktionen.</li>
<li>Ein PDF-Betrachter.</li>
<li>Alles, was du brauchst, um Archive (ZIP usw.) zu entpacken.</li>
<li>Und vieles Weitere.</li>
</ul>

<p>Du kannst sofort mit der Arbeit loslegen.</p>

