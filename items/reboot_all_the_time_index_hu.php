<h2>Belefáradtál már abba, hogy folyton újraindítsd a géped?</h2>

<p>Épp csak pár dolgot frissített a windows-os rendszerén a Windows Update-tel? Kérjük, indítsa újra számítógépét! Épp csak néhány új programot telepített? Kérjük, indítsa újra számítógépét! A rendszere instabilnak tűnik? Próbálkozzon egy újraindítással, utána már valószínűleg minden rendben lesz.</p>

<p>A Windows mindig azt kéri, indítsd újra a számítógépet, és ez elég bosszantó tud lenni (talán épp egy hosszú ideig tartó letöltés van folyamatban, és nem akarod megszakítani, csak mert pár dolgot frissítettél a rendszeren). De ha rákattintasz az „Újraindítás később” gombra, a Windows tovább zaklat téged, csak hogy tudassa veled, tényleg újra kellene indítani a gépet. És ha te épp nem vagy a géped előtt és nem látod a kérdést, automatikusan újraindul. Viszlát, sokáig húzódó letöltés!</p>

<p>A Linuxot alapvetően nem kell újraindítani. Akár új programokat telepítesz (még ha nagyon nagyokat is) vagy rutin frissítést végzel, nem fog megkérni, hogy indítsd újra. Ez csak akkor szükséges, ha a rendszer legbelsőbb részeit érinti a frissítés, és ez csak minden sokadik héten egyszer fordul elő.</p>

<p>Tudod, mik az internetszerverek? Ezek nagy számítógépek, amelyek válaszolnak neked, mikor weboldalakat kérsz le, és információkat küldenek a böngésződnek. A legtöbbjük Linuxot használ, és mivel mindig elérhetőnek kell lenniük (bármikor jöhet egy látogató), nem túl gyakran indítják őket újra (ugyanis a szolgáltatások ez idő alatt nem elérhetőek). Tulajdonképpen sokukat nem is indítják újra <b>évekig</b>. A Linux stabil, mindig tökéletesen fut újraindítás nélkül is.</p>

<p>Valószínűleg nem hagyod bekapcsolva a géped hetekig, de a lényeg: a rendszer nem fog állandóan az újraindítással zaklatni.</p>

