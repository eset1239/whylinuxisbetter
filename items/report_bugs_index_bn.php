<h2>সহজে বাগ রিপোর্ট, ট্র্যাক করুন।</h2>



<p>যেকোন প্রোগ্রামে বাগ (bug) বলতে বোঝায় প্রোগ্রামে অনিচ্ছাকৃত ত্রুটি। যেমন ধরুন আপনি গান শোনার জন্য একটি প্লেয়ার সফটওয়্যার ইন্সটল করেছেন। কিন্তু দেখা গেল একটি নির্দিষ্ট ফাইল ফরমেটের অডিও গান চালাতে গেলে পুরো প্রোগ্রামটাই ক্র্যাশ করে বসছে। সেক্ষেত্রে এটা এই প্রোগ্রামের একটা বাগ। সব প্রোগ্রামারই চান তাদের প্রোগ্রাম শতভাগ বাগমুক্ত হোক। কিন্তু যত দক্ষ প্রোগ্রামারই হোন কিংবা যত বড় সফটওয়্যার কোম্পানিই হোক, কেউই হলফ করে বলতে পারবে না যে তাদের প্রোগ্রামে একটিও বাগ নেই। লিনাক্স হোক কিংবা মাইক্রোসফটের উইন্ডোজই হোক, বাগ থাকলে তাতে আশ্চর্য হবার কিছু নেই।</p>


<p>
ধরুন Windows এর একটি বাগ পেলেন, কি করবেন? মাইক্রোসফটে রিপোর্ট করবেন? ভুলে যান (মাইক্রোসফটে বাগ রিপোর্ট করা নিয়ে একটা ইন্টারেস্টিং আর্টিকেল দেখতে পারেন <a href="http://www.oreillynet.com/mac/blog/2002/06/mission_impossible_submitting.html">এখানে</a>)। তাহলে কি করবেন? হাত তুলে খোদার কাছে সাহায্য চাইতে পারেন যেন বাগটা মাইক্রোসফটের লোকজনের নজরে আসে। আর এই বাগের কারনে যদি আপনার কম্পিউটার/নেটওয়ার্কের নিরাপত্তা হুমকির মুখে পড়ে তবে? বেশ, বন্ধুবান্ধব আর সহকর্মীদের নিয়ে প্রার্থনায় নেমে পড়ুন।</p>
<p>বাগটা যদি কখনোই মাইক্রোসফটের নজরে না পড়ে তবে? আশা করতে থাকুন Windows এর পরবর্তী রিলিজে এই সমস্যাটা থাকবে না। অবশ্য সেটা নিশ্চিত করতে হলে কয়েকশ' ডলার খরচ করে পরবর্তী রিলিজটা আবার কিনতে হবে। (ধরে নেয়া হচ্ছে পরবর্তী রিলিজ বের হওয়ার আগ পর্যন্ত আপনার নেটওয়ার্ক টিকে থাকবে)</p>



<p>লিনাক্স ডিস্ট্রিবিউশনগুলো সহ প্রায় সব ওপেন সোর্স সফটওয়্যারের নিজস্ব <i>বাগ ট্র্যাকিং সিস্টেম</i> আছে। এখানে বাগ রিপোর্ট করা খুবই সহজ। আর বাগ রিপোর্ট করাতেই শেষ নয়, আপনি চাইলে দেখতে পারেন বাগটা কিভাবে সমাধান করা হচ্ছেঃ সবকিছু সবার জন্য উন্মুক্ত। ডেভেলপাররা আপনার প্রশ্নের উত্তর দেবেন, হয়তো বাগ সম্পর্কে জানতে আপনাকে আরো প্রশ্ন করবেন। বাগ সমাধান হয়ে গেলে আপনাকে সাথে সাথে জানানো হবে, এবং আরো জানানো হবে কোথায় প্রোগ্রামটির নতুন ভার্সন পাওয়া যাবে (ফ্রি, নিঃসন্দেহে)। ওপেন সোর্স কমিউনিটিতে আপনি এমন অনেক মানুষ পাচ্ছেন, যারা আপনার সমস্যার সমাধান করে দিবে, আপনাকে সমাধানের অগ্রগতি সম্পর্কে অবগত রাখবে এবং এসব কিছুই সম্পূর্ণ ফ্রি। যে সমস্যা আপনি খুজে পেয়েছেন সেটা আপনার একার নয়, সবার... সমাধানটাও আপনার একার জন্য না, সেটা সবার জন্যেই। একটা সফটওয়্যার উন্নয়নে সবাই সমানভাবে আগ্রহী... আর এটাই ওপেন সোর্স কমিউনিটির মূলমন্ত্র।</p>

