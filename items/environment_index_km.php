<h2>បរិស្ថាន​</h2>

<p align="center">សូម​អរគុណ​ Franz Bourlet ចំពោះ​គំនិត​នេះ​​</p>

<p>អ្នក​អាច​សួរថា តើ​លីនុច​​​ខុស​ពី​វីន​ដូ​យ៉ាង​ដូច​ម្តេច​​ដល់​​បរិស្ថាន​​ ? បន្ទាប់មក​ពួកវា​ទាំង​ពីរ​គ្រាន់​តែ​ជា​ផ្នែក​នៃ​កម្មវិធី​ដែលមាន​ផលប៉ះពាល់​តិចតួច​ដល់​បិរស្ថាន ឬ​ការ​ផ្លាស់ប្ដូរ​អាកាសធាតុ ។ 
មែន​ហើយ ការជ្រើស​លីនុច​​ពិតជា​មាន​ឥទ្ធិពល​លើ​បរិស្ថាន​ ៖</p>

