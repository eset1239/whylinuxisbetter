<p>マイクロソフトはアメリカ合衆国の企業で、おかげでアメリカ経済はずいぶんと潤いました。</p>

<p>けれど、アメリカ合衆国以外では、この私的所有ソフト（つまりWindows）を買ったら、その売上げの半分はまっすぐにソフト会社の本部（つまりマイクロソフト）に行ってしまう。半分が国外流出し、残りの半分はどこかへ消えてしまうわけ（販売手数料など。技術分野には還元されません）。販売された国では何ひとつ生産されず、パッケージ販売のためには教育さえ必要じゃありません。つまり、IT専門家といっても高度な知識はなく、ただ特定の私有ソフトのインストールと設定だけできるという状態。ソフトを変更することも、ソフト開発を学ぶことも、カスタマイズすることもできないんだから。</p>

<p>フリーなソフト（つまりLinux）なら、その国の経済もIT専門家の知識も向上していくよね。なぜって、ソリューションをカスタマイズし、サポートを提供し、コンサルティングをするためにたくさんの中小企業が必要になってくるから。</p>

<p>技術を持ち、資金を国外に流出させないほうが、単純にマニュアル通りに箱を売って売上げを海外に送金し、実際にどういう仕組みになっているのかの本当の知識をIT専門家から隠すよりもずっといいとは思いませんか？</p>

