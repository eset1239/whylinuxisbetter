<h2>Pamirškite tvarkykles (draiverius)</h2>

<p>Prie kiekvienos naujos techninės įrangos, netgi pačios paprasčiausios, būna kompaktinis diskas. Tame diske yra labai mažai vietos užimanti programėlė, kuri vadinasi tvarkyklė (draiveris). Jei perskaitysite naudojimosi instrukciją, suprasite, kad Windows'uose techninė įranga neveiks arba veiks blogai kol neįdiegsite tvarkyklės. Jeigu jūs esate kaip daugelis ir neskaitote naudojimosi instrukcijos, tada tikriausiai pats susiprasite, ką reikia daryti, kai pamatysite, kad jūsų naujai pirktas daiktas neveikia:</p>

<p>Turite įdėti diską į diskų įrenginį, įdiegti tvarkyklę, šiek tiek palaukti, kol diegimas baigsis, ištraukti diską, perkrauti kompiuterį.</p>

<p>Jei techninę įrangą pirkote šiek tiek seniau ir ją norite naudoti prie kito kompiuterio, tikriausiai būsite užmiršęs, kur padėjote diską, o tada teks tvarkyklės ieškoti internete, kompiuterio dalies gamintojo puslapyje. O tai gali užtrukti, žiūrint kaip gerai yra sukonstruotas dalies gamintojo tinklalapis.</p>

<p>Gerai, susitvarkėme tik su viena kompiuterio dalimi. Įsivaizduokite, kad norite įdiegti Windows'us į naujutėlaitį, šviežią, nepaliestą kompiuterį. Kiekvienai kompiuterio daliai reikės susirasti naujausią tvarkyklės versiją (arba susirasti diskus), jas visas įdiegti, karts nuo karto perkrauti kompiuterį. Viso to reikės: vaizdo plokštei, garso plokštei, klaviatūrai, pelei, pagrindinei plokštei ir t.t. (geriau pradėti nuo vaizdo plokštės tvarkyklės, antraip galėsite dirbti su labai stambia skiriamąja geba ("rezoliucija")). O visa tai galėsite susitvarkyti tik po ilgo Windows'ų diegimo.</p>

<p><b>Linux'ams nereikia atskirų tvarkyklių</b>. Visos tvarkyklės yra įdiegtos į Linux branduolį, o tai reiškia:</p>

<ul>
<li>Įdiegiant Linux'us, užtrunkame labai nedaug laiko. Kai Linux'ai jau įdiegti, jūs turite viską, ko reikia pradėti darbui (netgi programinę įrangą, apie kurią galite paskaityti šios svetainės skyrelyje "Kai sistema jau įdiegta, kodėl jums vis dar reikia įdiegti visa kita?").</li>
<li>Visa techninė įranga veikia tuojau pat.</li>
<li>Mažiau kenkiame gamtai, nes visoms kompiuterio dalims nereikia diskų su tvarkyklėmis (na, tik kai kurioms, kurios yra gaminamos specialiai Windows'ams...).</li>
</ul>

