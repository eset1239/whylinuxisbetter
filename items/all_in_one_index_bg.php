<h2>Когато системата е инсталирана, защо <i>все още</i> да има нужда от инсталиране на разни неща?</h2>

<p>Инсталирането на Windows e само началото. Представете си, че вие току що сте си инсталирал чисто новото копие на Windows 7 и се каните да отприщите компютърните си умения. Приятел ви изпраща е-поща със прикачен PDF файл: по дяволите, нямате програма да го прочете. Трябва да отидете онлайн, да търсите уеб сайт, който да ви позволи да изтеглите Adobe Reader (или друг PDF четец), да го изтеглите, инсталирате, може би и дори да рестартирате. Фю, готово, всичко е настроено. Прикачен към е-пощата на приятеля си, намирате текстов документ, file.doc. Windows точно сега не може да чете и него: чудесно. Или ще идете да купите вашето копие на Microsoft Office, или просто ще свалите OpenOffice, но и така, трябва да го намерите, свалите (да се надяваме, че имате широколентова връзка), инсталирате и т.н. Приятелят ви е изпратил и изображение, но то е с лош контраст, лоша осветеност и има нужда от добро изрязване. Така че, вие сега може да отидете и да си купите Photoshop (и отново, това колко 'стотин кинта е), или да си свалите the GIMP (това е името на свободна програма, която може да прави почти толкова неща колкото и Photoshop): търсене, сваляне, инсталиране и т.н. Достатъчно: схванахте идеята, Windows е далеч от завършен и да го инсталирате е само началото на грижи.</p>

<p>Когато вземете Линукс (като Ubuntu, Mandriva, Fedora и т.н., те са различни варианти на Линукс), вие също получавате <i>без да инсталирате нищо повече</i> :</p>

<ul>

<li>Всичко, от което се нуждаете за да пишете текстове, да редактирате таблици, да правите добре оформени презентации, да чертаете, да редактирате уравнения.</li>

<li>Уеб четец (напр. Firefox) и програма за е-поща (напр. Thunderbird или Evolution).</li>
<li>Редактор на изображения (GIMP) почти толкова мощен колкото Photoshop.</li>
<li>Клиент за моментална комуникация.</li>
<li>Програма за гледане на филми.</li>
<li>Програма за слушане и организиране на музика</li>
<li>PDF четец</li>
<li>Всичко, от което се нуждаете за да разопаковате архиви (ZIP и т.н.).</li>
<li>и т.н.</li>
</ul>

<p>Просто може да започнете работа веднага.</p>

