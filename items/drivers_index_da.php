<h2>Glem alt om drivere</h2>

<p>Nye stykker hardware, selv den simpleste form, kommer normalt med en CD. På den CD er der et meget lille stykke software som kaldes en "driver". Hvis du læser instruktions manualen, vil du få at vide, at hardwaren ikke vil arbejde på en Windows-computer, før du har installerer driveren. Hvis du er som de fleste mennesker og ikke læser manualen, så finder du nok selv ud af det, når du ser din nye high-tech dims ikke virker med det samme.</p>

<p>Indsæt CD'en, klik på installations guiden, vent, skub CD'en ud, genstarte computeren.</p>

<p>Hvis du har købt hardwaren for et stykke tid siden og for igen at bruge det på en anden computer, vil du sikkert gerne glemme CD'en og hente den nyeste version af driveren fra producentens websted. Som kan tage en hel del tid, givet hvor underligt arrangeret nogle fabrikanters websteder er.</p>

<p>Okay, det er kun ét stykke hardware. Forestil dig så at du vil installere Windows på en helt ny computer, uberørt, computer. For hver enkelt lille stykke hardware skal du finde den nyeste driver (eller bruge en CD), installere det, and genstarte fra gang til gang. Grafikkort, lydkort, tastatur, mus bundkort chipset, mm. (det er nok bedst at tage grafikkortet først, ellers er du tvunget til at bruge en meget lav opløsning på din nye fladskærm). Og det kommer efter en ellers lang nok installation af Windows selv.</p>

<p><b>Linux har ikke brug for separate drivere</b>. Alle driverene er allerede inkluderet i Linux kernen, systemets kerne, og den er med i hver enkelt Linux installation. Dette betyder:</p>

<ul>
<li>En <b>meget hurtig og enkeltstående installation proces</b>. Når du er færdig, så har du alt hvad du behøver for at starte på at arbejde (inklusiv de programmer du skal bruge, se "Når systemet er installeret..." dellen her på siden).</li>
<li><b>Lige-fra-starten-klar</b> periferiudstyr.</li>
<li><b>Mindre skade for planeten</b> fordi alle de CD'er ikke behøver at følge med hardwaren mere (nå ja, i det mindste når Windows heller ikke behøver dem mere...).</li>
</ul>

