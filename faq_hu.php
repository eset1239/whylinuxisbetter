<h2>Egy kis GY.I.K. Linux kockáknak</h2>

<p>Rendben, kezdhetném azzal, hogy, ahogy már valószínűleg ti is észrevettétek, ez a weboldal nem igazán nektek készült :) A Windows- (és Mac-) felhasználókat próbálja rávezetni arra, mi is a „Linux”, hogy néz ki, és bemutatni nekik, hátha megtetszik nekik is valami. Valószínűleg nem találsz semmilyen új információt itt, szóval olvasgathatod tovább a Slashdotot! :-p</p>

<ul>

<li><p><b>K: A Linux aligha egy teljes operációs rendszer, az csak egy kernel. Miért hiteted el másképp a felhasználókkal?</b></p> 
<p>V: Szent Linus, kérlek, bocsáss meg nekem! :) Ez egy önkényes egyszerűsítés. Ha elkezdenéd magyarázni, hogy igazság szerint a Linux egy kernel neve, és a különöző disztribútorok kiválasztják, milyen más programokkal együtt alkossák meg a „Linux disztribúciójukat” stb., az olvasók fele már le is lépett. Ezt a weboldalt nem azok olvassák, akik már Linuxot használnak fő operációs rendszerként (hacsak nem akarnak az oldal fejlesztésében segíteni). Ugyanezen okból kifolyólag, és az egyszerűség kedvéért, nem beszéltem a különböző BSD-kről sem, és hogy sok <i>nagy</i> szerveren BSD fut, nem Linux.</p> 
</li>

<li><p><b>K: Miért mondod egy operációs rendszerre, hogy jobb, mint a másik? Ez attól függ, mire akarják használni.</b></p>
<p>V: Természetesen, tökéletesen egyetértek. De tisztázzuk, mit is próbálok itt mondani: 1) Hadd tudják meg a Windows/Mac OS X felhasználók, hogy más is létezik. 2) Mondjunk néhány okot, miért is tényleg jó rendszer a Linux, és legtöbbször miért ez a legjobb választás. Persze az oldal neve sokkal egyszerűbb ennél, de a francba is, talán a <tt>whylinuxmaybebetterinmanycases.net</tt> (miért lehet jobb sok esetben a Linux) címet kellett volna megvennem?</p>

<p>Nem vagyok a Linux-szakértő, sem valami szekta vezetője, és nem akarom hirdetni az igét: csak egy redkívül boldog Linux felhasználó és -fejlesztő vagyok, aki megpróbálja megismertetni a Linuxot az emberekkel.</p>

</li>

<a name="gnu"><li><p><b>K: GNU/Linux a neve, nem szimplán Linux, hülye!</b></a></p>

<p>V: Egy haladó Linux-felhasználóknak szóló oldalon egy ilyen részlet számít, én viszont szeretném, ha minden leegyszerűsített maradna. Az emberek a „Linuxról” hallottak, nem a „GNU/Linuxról”, és ez a tipikus „kocka-vita” is valószínűleg egy olyan dolog, ami akadályozza a "normális" embereket abban, hogy megértsék, miről is szól ez az egész. Ugyanezen okból kifolyólag írtam néhol „OpenOffice”-t az „OpenOffice.org” helyett és így tovább.</p>

<p>És egyébként ez a „K:” nem kérdés volt, a kérdőjelet sem látom a végén. De ha egyszer kérdéseket és válaszokat írok... Ó, haver, tényleg hülye vagyok.</p>

</li>

<li><p><b>K: Ember, pocsék az angolod!</b></p>
<p>V: Igen, tudom. És dolgozom rajta. Ne emlegessétek állandóan, légyszi!</p>
</li>

<li><p><b>K: Úgy tűnik a GNOME-ra és az Ubuntura koncentráltál. Netalán negatív érzelmeket táplálsz a KDE és a többi disztribúció iránt?</b></p> 
<p>V: Természetesen nem. Szeretem a KDE-t és sok KDE-s alkalmazást használok (ha minden erőt, amit a GNOME vs KDE vitákba fektetnek a trollok, inkább arra használnák, hogy a KDE-s alkalmazásokat szépen integrálják GNOME-ba és viszont, igazán boldog lennék). A képernyőképeim főleg az Ubuntut mutatják, hisz ezt a disztribúciót használom és fejlesztem. Mondanom is felesleges, hogy küldjetek bátran más képernyőképeket nekem. Biztos észrevettétek, hogy van egy rész az AmaroKról: ne tétovázzatok, küldjetek nekem további javaslatokat más KDE-s (és GNOME-os!) alkalmazásokra.</p> </li>

<li><p><b>K: Mégis mi a fenét tudsz te a Linuxról?</b></p> 
<p>V: Valószínűleg nem annyi dolgot, amennyit kellene! Az Ubuntu egyik tagja vagyok, Linuxot használok kb. 1998 óta, írtam pár jegyzetet (franciául) néhány nyílt forráskódú szoftverről (Firefox, Linux stb.). De próbálok fejlődni, épp nemrég rendeltem meg az „Understanding the Linux kernel” („A Linux kernel megértése”) című könyvet (mire ezt olvassátok, valószínűleg már végeztem is vele, megértettem mindent, amit csak a Linuxról tudni lehet és készülök megalkotni a saját verzióm, aminek kétségtelenül „Manux” lesz a neve -- rendben, lehet, hogy mégsem).</p> </li>

<a name="gaming"><li><p><b>K: Azt mondtad, a számítógépes játékosoknak nem kell Linuxra váltaniuk, de nem ismered a Wine-t, Cedegát, Steam? Egyre több játékkal működnek remekül.</b></p>
<p>V: Egyetértek, számos szoftver jelent megoldást, hogy a windows-os játékok Linuxon jobban fussanak, mint régebben. Ez megfelel az alkalmi játékosoknak. Észrevehetted azonban, hogy a "megrögzött játékosok" kifejezést használtam. Akik mindig megveszik a legújabb játékokat és túlhúzzák CPU-jukat, hogy nagyobb teljesítményt érjenek el, kétlem, hogy hajlandóak lesznek efféle kompromisszumokra.</p></li>

<a name="distros"><li><p><b>K: Kérlek, add hozzá az XY Linux disztribúciót az oldalad telepítésről szóló részéhez!</b></p>
<p>V: Sok-sok disztribúció létezik, és ezek egyetlen valódi részhalmaza sem felelne meg mindenkinek. Azok számára, akik ki akarják próbálni a Linuxot, már így is elég zavaros, hogy ilyen sok "verzió/változat" van belőle. Úgy döntöttem, 4-et említek meg közülük, melyek régebben is népszerűek voltak és ma is azok. Szerintem ez egy ésszerű ok.</p></li>

<li><p><b>K: Hé, nem te vagy az a dilis <a href="http://www.manucornet.net/pcjacking">PCJacking</a>-es fazon?</b></p>
<p>V: Hopsz, de igen.</p>
</li>

<li><p><b>K: Hé, nem te vagy az a dilis <a href="http://www.manucornet.net/Informatique/Gimp_Texturize.php">GIMP-es textúrázó</a> fazon?</b></p>
<p>V: Rendben, lebuktam :) De <a href="http://jb.rouquier.free.fr/">Jean-Baptiste Rouquier</a> végezte a munka másik felét. Jó, a nagyobb részét. Hú, kérem a következő kérdést!</p>
</li>

<li><p><b>K: Hé, nem te voltál a 400 <a href="http://code.google.com/summerofcode.html">Google Summer of 
code-os</a> dilis fazon közül az egyik, akiket 9000 közül választottak ki?</b></p> 
<p>V: Igen. De csak szerencsém volt: 8000-et én küldtem be a 9000 projekt közül, és az egyiket elfogadták. Rendben, kétszer is szerencsém volt (2005, 2006). Jövőre közzéteszem az automatikus projektgenerátorom forráskódját. </p> </li>

<li><p><b><a name="help">K: Szia! Nem működik az XY dolog Linuxon. Segítenél nekem?</a></b></p>

<p>V: Sajnálom, de az e-mail címemre nem úgy kéne gondolni, mint egy állandó segélyvonalra. :-) Mint bárki másnak, nekem is sok dolgom van, és nagyon örülök, ha segíthetek valakinek, amikor tehetem és van időm rá. De biztos vagyok benne, hogy sok ember van a fórumokon és az IRC-csatornákon, akik több mint boldogan válaszolnának <a href="http://www.whylinuxisbetter.net/items/help/index.php">a kérdéseidre</a>! És a <a href="http://www.google.com">Google</a> keresője mindig jó kiindulási pont.</p></li>

