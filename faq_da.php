<h2>Nogle FAQ for Linux nørder</h2>

<p>Okay, jeg starter lige med at sige, som du nok har gættet, så er denne webside ikke rigtig ment til dig :) Den er mere ment for Windows (eller Mac) brugere som vil finde ud af hvad "Linux" er, hvordan det ser ud, og se om det er noget de kan bruge. Du finder nok ikke nogle nye informationer her, og du kan nok lige så godt blive ved med at læse slashdot :-P</p>

<ul>

<li><p><b> Q: Linux er ikke ligefrem et fuldt OS ligesom Windows, det er jo kun kernen. Hvorfor fortælle folk noget andet ?</b></p>

<p>A: Skt. Linus tilgiv mig :) Dette er frivillig forenkling. Hvis du begynder at forklare dette, sagen er at Linux er kernens navn, og at leverandøre vælger at bruge Linux sammen med deres software og laver en "Linux distribution", ..., og halvdelen af publikum er allerede væk. Det her er ikke en webside for folk der allerede bruger Linux som deres foretrukne OS (med mindre de vil være med til at gøre siden bedre). Dette er også grunden til, og for at gøre det mere enkelt, at jeg ikke snakke om de forskellige variationer af BSD og det faktum at mange af de store servere kører BSD og ikke Linux.</p> 
</li>

<li><p><b>Q: Hvorfor siger du at ét Operativsystem er bedre end andre? Det kommer jo an på hvad folk vil bruge det til.</b></p>
<p>A: Selvfølgelig gør det det. Jeg er helt enig. Men lad os lige blive enige om hvad det er jeg prøver at fortælle her: 1) Lad Windows/Mac OS X brugere vide at der findes andet derude. 2) Give et par gode grunde til hvorfor Linux er et godt system, og hvorfor det i mange tilfælde er et godt valg. Klart, sidens navn antyder måske mere end det, men hey, skulle jeg måske have købt <tt>whylinuxmaybebetterinmanycases.net</tt> ?</p>

<p>Jeg er ikke nogen Linux ekspert, eller nogen sekt leder, jeg vil ikke prædike: Jeg er bare en meget glad bruger og udvikler af Linux, jeg forsøger bare at gøre flere mennesker opmærksom på Linux.</p>

</li>

<a name="gnu"><li><p><b> Q: Det er GNU/Linux, ikke Linux, idiot!</b></a></p>
<p>A: På en hjemmeside for avancerede Linux-brugere ville den slags oplysninger betyde noget. Men lad os prøve at holde det simpelt. Folk har hørt om "Linux", ikke "GNU/Linux", og denne form for nørdede skænderier er formentlig en af de ting, der forhindrer "normale" mennesker i at forstå, hvad det handler om. Af samme grund, skriver jeg "OpenOffice" i stedet for "OpenOffice.org" og så fremdeles.</p>

<p>Og for resten, det her "Q:" er slet ikke et spørgsmål, eftersom det ikke slutter med et spørgsmålstegn. Men alligevel, siden jeg skriver både spørgsmål og svar... jeg er sgu en kegle.</p>

</li>

<li><p><b>Q: Dit engelsk er råddent.</b></p>
<p>A: Ja, jeg ved det godt. Jeg arbejder på det. Giv bolden videre, tak.</p>
</li>

<li><p><b>Q: Du er rimelig fokuseret på GNOME og Ubuntu. Har du noget imod KDE og andre distributioner ?</b></p>
<p>A: Selvfølgelig ikke. Jeg elsker og benytter en masse KDE programmer (hvis al den energi der bliver brugt på GNOME vs. KDE-diskussioner, kunne blive investeret i ordenligt at integrere KDE programmer i GNOME og den anden vej, så ville det gøre mig glad). Mine screenshots er mest fra Ubuntu fordi det er den distribution jeg bruger og udvikler for. Du er selvfølgelig velkommen til at sende mig andre screenshots. Som du nok har opdaget så har jeg en forkærlighed for AmaroK, men send mig endelig yderligere foreslag til andre KDE (og GNOME!) programmer.</p> </li>

<li><p><b>Q: Hvad fanden ved du om Linux egentlig ?</b></p> 
<p> A: Sikker ikke så meget som jeg burde! Jeg er medlem af Ubuntu, jeg har brugt Linux siden 1998. Har skrevet et par pocket-books (på fransk) om Open Source software (Firefox, Linux, mm.). Men jeg arbejder på sagen. Jeg har lige bestilt "Understand the Linux kernel"-bogen (når du læser dette, er jeg nok færdig med at læse den, forstå alt der er at vide omkring Linux og ville lave min egen version, som jeg helt sikkert kommer til at kalde "Manux" -- okay, måske ikke).</p> </li>

<a name="gaming"><li><p><b> Q: Du sagde gamere skal ikke skifte til Linux, men kender du ikke til Wine, Cedega, Steam? Disse fungerer med et stigende antal af Windows-spil.</b></p>
<p>A: Jeg er enig i at forskellige software løsninger gør det at køre Windows-spil under Linux, meget nemmere end det plejede at være. Det er en god løsning for hygge-gamere. Bemærk dog, at jeg brugte udtrykket "hardcore gamere", dem der køber de nyeste spil og overclocker deres CPU'er for at få en bedre ydeevne. Jeg tvivler på at disse er villige til at gå på kompromis.</p></li>

<a name="distros"><li><p><b> Q: Tilføj venligst Linux-distributionen XYZ til din side om at installere Linux.</b></p>
<p>A: Der er mange, mange distributioner derude, og det er umuligt at tilfredsstille alle. For folk, der ønsker at afprøve Linux, er det allerede lidt forvirrende, at der er flere "udgaver/varianter" af det. Jeg valgte at nævne 4 af dem, der har været og stadig er blandt de mest populære, jeg synes, det er et rimeligt valg.</p></li>

<li><p><b> Q: Hey, er det ikke dig den tossede <a 
href="http://www.manucornet.net/pcjacking">PCJacking</a> fyr ?</b></p>
<p>A: Ups, jo.</p>
</li>

<li><p><b>
Q: Hey, er det ikke dig den tossede <a 
href="http://www.manucornet.net/Informatique/Gimp_Texturize.php">GIMP-Texturize</a> 
fyr ?</b></p>
<p>A: Jo, der fik du mig :) Men <a 
href="http://jb.rouquier.free.fr/">Jean-Baptiste Rouquier</a> lavede også halvdelen af arbejdet. Okay, lidt mere. Huh, lad os tage næste spørgsmål.</p>
</li>

<li><p><b>Q: Hey, er du ikke en af de skøre <a href="http://code.google.com/summerofcode.html">"Google's Summer of Code"</a>-fyre, altså en af de 400-ud-af-9000 udvalgte hackere ?</b></p>
<p>A: Jo. Men jeg var bare heldig: Jeg sendte 8000 af de 9000 projekt-forslag, og en af dem blev accepteret. Okay, jeg blev heldig to gange (2005, 2006). Næste år gør jeg min automatisk projekt generator, open source. </p> </li>

<li><p><b>Q: Hej! jeg kan ikke få XYZ til at køre på Linux. Kan du hjælpe mig?</b></p>
<p>A:Desværre, min email-adresse skal ikke betragtes som en kilde til ubegrænset Linux-support. Som så mange andre har jeg en masse ting at lave. Selvfølgelig er jeg glad for at hjælpe når jeg har tid til det, men jeg er sikker på mange folk derude på fora og i IRC channels vil være mere end glade for at <a href="http://www.whylinuxisbetter.net/items/help/index.php">Besvare jeres spørgsmål</a>! og en <a href="http://www.google.dk">"Google"</a> søgning er altid et godt sted at starte.</p> </li>
