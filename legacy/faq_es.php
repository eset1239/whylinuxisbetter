<h2>Un pequeño FAQ para los entusiastas de Linux</h2>

<p>Está bien, debo empezar diciendo que, como probablemente te habrás dado cuenta, este sitio web no es realmente 
para ti :) Está pensado para usuarios de Windows (o Mac) que quieren averiguar qué es "Linux", qué 
apariencia tiene, y ver si en él hay algo bueno para ellos. ¡Probablemente no encuentres ninguna información nueva 
para tí aquí y tal vez mejor sigues leyendo /.! :-p</p>

<ul>

<li><p><b>P: Linux difícilmente es un SO completo como Windows, sólo es el kernel. ¿Por qué hacer creer a los 
usuarios otra cosa?</be></p> <p>R: San Linus por favor perdóname :) Esta es una simplificación voluntaria. Si empiezas 
explicando esto: el hecho es que Linux es el nombre del kernel y los diferentes proveedores escogen empaquetar Linux 
con su elección de programas para hacer una "Distribución de Linux", etc., la mitad de la audiencia ya se habrá ido. 
Este no es un sitio web para gente que ya está usando Linux como su SO principal (a menos que quieran ayudar a 
mejorar este sitio y, honestamente, ¡lo necesito!).</p> </li>


<li><p><b>P: ¿Por qué estás diciendo que un SO es mejor que otro?. Depende de lo que la gente quiera hacer con 
él.</b></p> <p>R: Definitivamente así es, concuerdo perfectamente con eso. Pero dejemos claro lo que intento hacer 
aquí: 1) Hacer saber a los usuarios de Windows/Mac OS X que existe algo más. 2) Dar unas cuantas razones por las cuales 
Linux es un buen sistema y por qué la mayor parte del tiempo es una muy buena elección. Obviamente, el nombre del 
sitio web es más directo que eso, pero vamos, ¿debería haber comprado el nombre <tt>whylinuxmaybebetterinmanycases.net 
(porquélinuxpuedesermejorenlamayoríadeloscasos.net)</tt>?</p>

<p>No soy un experto en Linux, ni líder de una secta y no quiero evangelizar: sólo soy un usuario extremadamente 
feliz y desarrollador de software para Linux intentando que más gente escuche sobre él.</p>

</li>

