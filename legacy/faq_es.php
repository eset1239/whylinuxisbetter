todo esto. Por la misma razón, escribo "OpenOffice" en lugar de "OpenOffice.org" y ese tipo de cosas.</p>

<p>Y a propósito, esta "P" no es una pregunta ya que no tiene signo de interrogación. Pero no importa porque yo estoy 
