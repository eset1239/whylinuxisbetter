<h2>Linuxen jarraitzaileentzat FAQ txiki bat</h2>

<p>Ongi da, hasteko, eta seguraski ikusi duzun moduan, web hau ez dago benetan zuretzat pentsaturik :) Egia esanda, 
Windowsen (edota Mac-en) erabiltzaileentzat pentsatuta dago, "Linux" zer den jakin nahi dutenentzat, nolakoa den,
zein itxura daukan, eta ea beraientzat zeozer ona dakarren jakin nahi dutenentzat. Seguraski ez duzu ezer berririk topatuko
 hemen, eta hortaz, hobe slashdot irakurtzen jarraitzen baduzu! :-p </p>

<ul>

<li><p><b>G: Linux berez ez da erabateko SO bat, Windows bezala. Soilik kernel-a dugu. Zergaitik erabiltzaileei 
gauza ezberdina sinistera eraman orduan?</b></p> <p>E: Linus Deuna, mesedez barkaidazu :) Hau soilik erraztasun hutsa da.
Nahi izatekotan, honakoa azal dezakegu: Linux, berez, Kernel-aren izena da, eta hornitzaile ezberdin bakoitzak nahi
dituen programekin "paketatu" ditzake, honelaxe "Linuxen sailkapena (distribuzioa edo)" bat sortuz, etab. Baina seguraski
puntu honetara ailegatzerakoan irakurleen erdia jadanik joan egingo litzateke.
Hauxe ez da Linux bere SO gisa erabiltzen ari diren pertsonentzat pentsaturiko web bat (orrialdea hobetzen lagundu nahi
baldin badute, behintzat-- eta egia esanda, beharrezkoa da!!).</p> </li>


<li><p><b>G: Zergaitik ari zara esaten SO bat bestea baino hobeagoa dela? Auskalo, bakoitzak zertarako erabiliko duen, ezta?
</b></p> <p>
E:Behin betiko horrela da, erabat ados nago horrekin. Baina argi utziko dugu hemen egin nahi dudana: 1) Windows/Mac-en
erabiltzaileei zeozer gehiago existitzen dela jakinaraztea. 2) Linux sistema on bat dela esateko dauden arrazoi batzuk 
ematea, eta zergaitik gehienetan aukera on bat dela esatea. Noski, orrialdearen izena askosez zuzenagoa da, baina beno,
 <tt>whylinuxmaybebetterinmanycases.net (zegaitiklinuxgehienetanhobeagoaden.net)</tt> erosi beharko nuke hau sahiesteko.
 </p>

<p>Ez naiz Linuxen maisua, ezta sekta baten burua ere, eta ez dut inor ebangelizatu nahi: soilik pozik den erabiltzaile
bat naiz, software askea sortzen duen pertsona bat, eta gero eta jende gehiago honi buruz entzutea zaiatzen den 
pertsona bat. 
</p>

</li>

<li><p><b>G: GNU/Linux da, ez Linux, artaburua!!</b></p> <p>
E: Erabiltzaile aurreratuen leku batean xehetasun hau oso garrantzitsua izango litzateke. Baina gauzak erraz
utziko ditugu. Jendeak "Linux"-i buruz entzun du, ez "GNU/Linux"-i buruz, eta amorratuen arten sortzen diren 
eztabaida hauek dira, hain zuzen ere, jende "normala" guzti hau zertaz doan jakitera sahiesten duten arrazoi
bat. Arrazoi berdinagatik, "OpenOffice" idazten dut, "OpenOffice.org" en ordez, eta honela.</p>

<p> Esan behar dut "G" hau ez dela galdera bat, galdera ikurrik ez dagoelako. Baina berdin da, nik neuk galderak 
eta erantzunak idazten ari naizelako... bai, benetan artaburua naiz.</p> </li>


<li><p><b>G: GNOME eta Ubuntu-n sartu-saturik zaude. Zeozer al daukazu KDE eta bezte sailkapenen kontra?</b></p> 
<p>E: Noski ezetz. Beste KDE aplikazio batzuk erabiltzen eta maitatzen ditut (GNOME eta KDE-ren troll guztiak bi
aplikazio moten arteko integrazioan lan egingo balute, benetan pozik egongo nitzateke). Eta nire pantailaren irudiak
Ubuntu erakusten dute, nik erabiltzen dudan sailkapena delako, eta aplikazio ezberdinak sortzen dizkiodalako. Ez da
esan behar beste pantaila-irudi berri batzuk bidaltzeko aske zarela. Konturatuko zara AmaroK-i buruz sail bat daukadala.
Hortaz, ez pentsatu birritan KDE eta GNOMEren aplikazio ezberdinentzako iradokizunen bat bidaltzeko!
</p> </li>

<li><p><b>G: Orduan, zer demontre dakizu zuk Linuxi buruz?</b></p> <p>
E: Seguraski, ez nahi dudan hainbeste! Ubunturen kidea naiz, Linux ~1998-tik erabiltzen dut, patrika liburu batzuk
idatzi ditut (frantzesez) Kode Irekidun programa batzuei buruz (Firefox, Linux, etab.), baina hori konpundu beharrekoan
nago, ondino "Understand the Linux kernel" edo "Linuxen kernel-a ulertu" antolatzen ari naiz (seguraski zuk hau
irakurtzerakoan, jadanik bukatuta izango dut, Linuxi buruz nahi dudan guztia jakingo, eta nire sailkapena sortu nahiko
nuke, ziur "Manux" izenarekin-- ados, beharbada ez :-p).</p> </li>

