title: Instal·la Linux</h2>

<p>D'acord, així que has decidit canviar-te a Linux. No te'n penediràs.</p>

<p>Si vols mantenir Windows al teu ordinador (la qual cosa jo recomano, per al cas que canvies d'opinió, o per al cas que trobis alguna cosa que sols pots fer en Windows), Windows va a necessitar compartir el disc amb Linux. Aleshores, cada vegada que el teu ordinador s'iniciï, et permetrà triar entre Windows o Linux.</p>

<p>Primer hauries de fer una còpia de seguretat de totes les teves dades. Això és extremadament important. No sabem mai què pot anar malament. No, realment, no estic fent broma. Fes una còpia de seguretat de les teves dades, agafa un CD buit, un DVD buit, altre disc dur, el que sigui, però fes una còpia de seguretat del teu treball.</p>

<p>Llavors hauries de desfragmentar tot el teu disc dur. Efectivament ara, les teves dades estan escampades per tot arreu (Windows no gestiona això molt bé, Linux ho fa molt millor). Després de desfragmentar (això pot trigar unes poques hores depenent de la grandària del teu disc dur), totes les dades estaran recollides al principi del teu disc dur (pensa en ell com en una llarga cinta, amb un principi i un final). Si les teves dades estan recollides allà, Linux no tindrà cap problema en dividir (virtualment, per suposat :) ) el teu disc dur en dos, i instal·lar-se ell mateix a la segona part.</p>

