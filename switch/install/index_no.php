<p>Når du har valgt din distribusjon, last ned CD-bildene. Deretter brenner du CD-bildene på blanke CD-er eller DVD-er. Hvis du ikke er sikker på hvordan man gjør dette, bør du heller betale noen kroner for å en installerbar CD sendt hjem til deg: <a href="http://www.easylinuxcds.com">EasyLinuxCDs.com</a>. De har et godt rykte. Nå setter du inn CD-en og restarter datamaskinen. Nå bør CD-en din starte, og hvis den ikke gjør det, så må du gjøre om BIOS-innstillingene dine. (Se «Prøv Linux uten forandringer på din datamaskin»-delen av websiden.)</p>

<p>Etter dette følger du instruksjonene på skjermen – enkelt og greit.</p>

