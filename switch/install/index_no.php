title: Installer Linux</h2>

<p>OK, så du har bestemt deg for å bytte til Linux. Du kommer ikke til å angre!</p>

<p>Hvis du ønsker å beholde Windows på datamaskinen din (det anbefales, i tilfellet du ombestemmer deg, eller hvis du skal gjøre ting som bare er mulig på Windows), så deler Windows diskplass med Linux. Så, hver gang maskinen starter, vil du få valg mellom å starte Windows eller Linux.</p>

<p>Først og fremst <b>bør du lage en backup av all din data</b>. Dette er meget viktig. Du vet aldri om det kan gå galt. <b>Jeg mener det</b>. Sikkerhetskopier all din data, skaff blanke CD-er, DVD-er, en ny harddisk, hva som helst – bare backup din data.</p>

<p>Så bør du defragmentere harddisken. Nå er all din data spredt utover (Windows klarer ikke å håndtere data godt, Linux gjør det mye bedre). Etter defragmenteringen (dette kan ta noen timer, det spørs hvor stor harddisken din er) vil all din data ha samlet seg i begynnelsen av harddisken (tenk på det som en lang linær tape, med en begynnelse og slutt). Hvis din data er samlet der, vil ikke Linux ha problem med å splitte (virtuelt, selfølgelig :-) ) din harddisk i to deler, for å deretter installere seg selv på den andre delen.</p>

