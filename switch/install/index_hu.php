<p>Mikor kiválasztod a disztribúciót, töltsd le a CD képfájl(oka)t. Majd írd ezt (vagy ezeket) ki egy üres CD-re (vagy CD-kre). Ha nem vagy biztos a dolgodban és inkább áldoznál rá pár száz forintot, hogy a CD-t kiküldjék neked, próbáld meg az <a href="http://www.easylinuxcds.com">EasyLinuxCDs.com</a>-ot, elég jó visszajelzéseket hallani róluk. Most helyezd be az (első) CD-t, és indítsd újra a rendszert. A gépednek a CD-ről kell indulnia; ha mégsem ez történik, a BIOS konfigurációddal van a gond (lásd a „Próbáld ki a Linuxot!” című részben).</p>

<p>Ezután csak kövesd a képernyőn megjelenő utasításokat: általában egész egyszerűek és egyértelműek!</p>

<p><b>Jó linuxozást!</b></p>

