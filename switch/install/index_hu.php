title: Telepíts Linuxot!</h2>

<p>Oké, szóval eldöntötted, hogy Linuxra váltasz. Nem fogod megbánni!</p>

<p>Ha szeretnéd megtartani a Windows-t (amit abban az esetben ajánlok, ha esetleg még mindig nem vagy biztos a váltásban, vagy ha van valami, amit csak Windows-on tudsz megtenni), a Windows-nak meg kell majd osztania a lemezt a Linuxszal. Ezután minden alakalommal, amikor elindul a géped, választhatsz a Windows és a Linux közül.</p>

<p>Először is <b>mentsd ki minden adatod</b>! Ez rendkívül fontos. Sohasem tudhatjuk, mi fog elromlani. Nem, komolyan, nem viccelek. Mentsd ki az adatokat, kapd elő az üres CD-ket, DVD-ket, más merevlemezeket, vagy akármit, csak mentsd el valahová az adatokat!</p>

<p>Ezután töredezettségmentesítsd a merevlemezed! Most az adataid épp szanaszét vannak mindenfelé (a Windows nem kezeli ezt valami jól, a Linuxnak sokkal jobban megy). A töredezettségmentesítés után (ami néhány órát igénybe vehet a lemezed méretétől függően), minden adatod a merevlemezed elején fog csoportosulni (gondolj rá úgy, mint egy hosszú folytonos szalagra, egy kezdettel és egy véggel). Ha az adataid ott csoportosulnak, a Linuxnak nem fog problémát jelenteni a merevlemezed kettéosztása (természetesen virtuálisan :) ), és feltelepíti magát a második részre.  </p>

<p>Oké, meg is vagy mindennel, most már csak ki kell választanod, melyik Linux disztribúciót szeretnéd. A Linux különféle változatait különböző szervezetek, vállalatok építik fel, a szoftverek különböző összeállításaival, különböző kinézettel és hangulattal stb. Mind Linux, csak más „körítéssel”. Ez nem egy egyértelmű választás, és ebben nem segíthetek (sok ember amellett érvel, hogy az általa használt disztribúció a legjobb). Íme viszont a négy legnépszerűbb disztribúció, a <a 
