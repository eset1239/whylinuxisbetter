title: Instala Linux</h2>

<p>Valeu!, entón decidiche instalar Linux. Non o vas lamentar!</p>

<p>Se queres manter Windows na túa computadora (o que recomendo por se cambias de parecer posteriormente ou necesitas facer cousas que só se poidan facer en Windows), Windows vai compartir o disco duro con Linux. Cada vez que inicies a túa computadora, vaiche a deixar escoller entre Windows ou Linux.</p>

<p>Primeiro deberías facer copias de seguridade de toda a túa información. Isto é extremadamente importante. Nunca se sabe que pode saír mal (por exemplo unha caída de tensión). Non, realmente non estou chanceando. Fai copias de seguridade, consegue CDs en branco, DVDs en branco, outro disco duro, o que sexa, pero fai copias do teu traballo.</p>

<p>Logo vas a desfragmentar o teu disco duro. Neste momento a túa información está esparexida por todos lados do teu disco duro (Windows non manexa moi ben isto, Linux faio moito mellor). Logo de desfragmentar (pode levar varias horas, dependendo do tamaño do teu disco duro), toda a túa información estará ao comezo do teu disco duro (pensa nisto como unha cinta de vídeo longa, cun inicio e un final). Se a túa información é apilada aí Linux non vai ter ningún problema en partir (por suposto virtualmente :) ) o teu disco duro en dous, e instalarse así mesmo na segunda parte.</p>

<p>Valeu, xa estás listo, todo o que necesitas é escoller a distribución Linux que queres. Linux vén en moitos diferentes "sabores", feitos por diferentes organizacións ou compañías, con diferentes opcións de software, diferentes aparencias, etc. Todos eles son Linux, pero con diferentes "interpretacións". Esta non é unha elección obvia e aquí realmente non che podo axudar (de feito, a moita xente gústalle debater que as súas distribucións son as mellores). De todas as maneiras aquí están as catro distribucións máis populares, segundo <a href="http://www.distrowatch.com/index.php?language=ES">distrowatch.com</a> :</p>

