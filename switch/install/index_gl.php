<p>Cando escollas unha distribución, descarga a imaxe(s) do CD(s) e grávaa(s) en CD(s) en branco, introduce o primeiro CD na túa lectora e reinicia. O teu PC debería arrincar usando o CD; se non o fai, deberás cambiar a configuración do teu BIOS ou buscar quen o faga (ver a sección "Proba Linux").</p>

<p>Logo diso, segue as instrucións da pantalla: case sempre son bastante simples e sinxelas!</p>

