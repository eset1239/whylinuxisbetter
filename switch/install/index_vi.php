<p>Vậy là bạn đã định dùng thử Linux. Bạn sẽ không hối tiếc vì điều đó!</p>

<p>Nếu bạn muốn giữ lại Windows (tôi cũng khuyên như vậy, nếu bạn thay đổi ý định hoặc 
có vài điều bạn chỉ làm được trên Windows). Bạn cần chia xẻ ổ đĩa cứng cho cả Windows 
và Linux. Mỗi khi bật máy tính, bạn có thể lựa chọn hệ điều hành nào để khởi động.</p>

<p>Bạn nên <b>sao lưu tất cả dữ liệu trước</b>. Điều này cực kì quan trọng. Chúng ta 
