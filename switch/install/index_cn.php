<p>当您选择好后，下载它的CD镜像，把它烧录在空白CD（或者DVD）上，重启计算机，
在BIOS设置里将CD-ROM设为第一启动，放入烧录好的CD，重启电脑。您的计算机将会使用
CD来启动，如果没有，请仔细检查BIOS设置。</p>

<p>这之后，您只需要按屏幕上的提示操作：安装非常的简单明了，不需要多少时间就可以完成。</p>

