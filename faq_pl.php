<h2>Mała lista najczęściej zadawanych pytań (FAQ) dla Linuksowych maniaków</h2>

<p>Powinienem zacząć od tego, że strona ta nie jest przeznaczona dla Was,
ale to zdążyliście już pewnie zauważyć :). Grono odbiorców, na którym
szczególnie mi zależy, to przede wszystkim użytkownicy systemów Windows
i Mac OS'ów, którzy usiłują dowiedzieć się, co to takiego ten "Linuks",
jak wygląda i czy będzie im odpowiadał. Raczej nie znajdziecie tu niczego
nowego dla siebie, możecie więc równie dobrze wrócić do lektury
Slashdot'a! :P</p>

<ul>

<li><p><b>P: Linuks to wcale nie system operacyjny, to przecież tylko jądro
systemu. Czemu wprowadzasz ludzi w błąd?</b></p> <p>O: Wybacz mi, o Święty
Linusie :) - to zwykłe i raczej nieszkodliwe uproszczenie. Jeśli na wstępie
zajmiesz się tłumaczeniem, że Linuks to jedynie nazwa jądra i że różne firmy
tworzą wokół niego własne "dystrybucje" - to połowa czytelników w trakcie
Twojego wywodu zdążyła już zmienić lekturę. To nie jest strona dla ludzi
używających Linuksa na co dzień, jako głównego systemu (chyba, że chcą pomóc
w jej budowie -- albo poprawić mój angielski <i>[lub mój polski - przyp.
tłum. :) ]</i>, naprawdę tego potrzebuję!).</p> </li>

<li><p><b>P: Czemu twierdzisz, że jeden system jest lepszy od drugiego?
Przecież zależy to od tego, do czego ludzie zechcą go używać.</b></p>
<p>O: Oczywiście że tak, zgadzam się z tym całkowicie. Stronka ta powstała
aby: 1) Użytkownicy systemu Windows i Mac OS X'ów dowiedzieli się, że istnieje
coś jeszcze, 2) Przytoczyć kilka powodów, dla których to całkiem niezły
system i dlaczego w większości wypadków to bardzo dobry wybór. Oczywiście,
nazwa tej strony może budzić pewne wątpliwości, ale jest chyba lepsza niż
<tt>whylinuxmaybebetterinmanycases.net</tt>?</p>

<p>Nie jestem Linuksowym ekspertem, nie założyłem też żadnej sekty, nie
zamierzam nikogo ewangelizować: jestem po prostu bardzo zadowolonym
użytkownikiem i deweloperem Linuksa, który próbuje sprawić, by ludzie
wiedzieli o Linuksie nieco więcej.</p>

</li>

<li><p><b>P: Głupcze, to GNU/Linux, a nie Linux!</b></p>
<p>O: Na stronie dla zaawansowanych użytkowników Linuksa tego typu
detale miałyby znaczenie. Ale nie komplikujmy prostych spraw: ludzie
słyszeli o Linuksie, nie o systemie "GNU/Linux" i tego typu "fachowe"
nazewnictwo to prawdopodobnie jedna z przyczyn, dla których tak trudno
dotrzeć do "zwykłych" ludzi. Z tego samego powodu piszę też "OpenOffice"
zamiast "OpenOffice.org", itp., itd.</p>

<p>Tak poza tym, to "P: " powyżej oznacza pytanie, tymczasem nie
zauważyłem tam znaku zapytania. Zresztą, sam piszę zarówno pytania,
jak i odpowiedzi... faktycznie, jednak jestem głupi.</p>

</li>

<li><p><b>P: Człowieku, Twój angielski <i>[polski? - przyp. tłum. (hehe ;-) - przyp. 2 tłum.)]</i>
jest do bani.</b></p>
<p>O: Tak, wiem o tym. Pracuję nad tym. Następny proszę!</p>
</li>

<li><p><b>P: Wydajesz się koncentrować na GNOME i Ubuntu. Czyżby jakiś uraz
do KDE i innych dystrybucji?</b></p> <p>O: Oczywiście że nie. Bardzo sobie
cenię KDE i często używam aplikacji z KDE (gdyby tak cały wysiłek trolli
biorących udział w wojnie pt. "GNOME kontra KDE" zainwestować w łączenie
tych środowisk... no cóż, wówczas byłbym szczęśliwszy). Zrzuty ekranów na
tej stronie zwykle pokazują Ubuntu, ale to dlatego, że tej właśnie
dystrybucji używam i dla niej tworzę programy. Żeby nie przedłużać... jeśli
chcesz znaleźć tu więcej zrzutów ekranu z innych dystrybucji czy środowisk
graficznych, prześlij je do mnie. Zwróć też uwagę na oddzielną podstronę
o programie AmaroK. Nie wahaj sie też ani chwili, jeśli masz jakieś cenne
wskazówki odnośnie programów pod KDE (i GNOME!) - prześlij je do mnie.</p> </li>

<li><p><b>P: A co ty niby u diabła wiesz o tym całym Linuksie?</b></p>
<p>O: Pewnie nie za wiele, mniej niż powinienem! Jestem członkiem społeczności
Ubuntu, Linuksa używam od circa 1998 roku, napisałem kilka książeczek
(po francusku) w temacie Wolnego Oprogramowania (m. in. Firefox, Linuks itp.).
Wciąż jednak próbuję naprawić obecny stan mojej (nie)wiedzy, właśnie zamówiłem
książkę "Understand the Linux kernel" <i>["Zrozumieć jądro Linux"]</i>
(w momencie, gdy to czytasz, być może już ją przeczytałem, dowiedziałem się
wszystkiego, czego tylko można o Linuksie i stworzyłem własną jego dystrybucję,
oczywiście o nazwie "Manux"... no dobra, pewnie jeszcze nie :P.</p> </li>

<li><p><b>P: Czy to nie Ty jesteś tym dziwakiem od <a 
href="http://www.manucornet.net/pcjacking">PCJacking</a>?</b></p>
<p>A: No cóż, owszem.</p>
</li>

<li><p><b>P: Czy to nie Ty jesteś tym dziwakiem od GIMP'owej wtyczki <a 
href="http://www.manucornet.net/Informatique/Gimp_Texturize.php">GIMP-Texturize</a> 
?</b></p>
<p>O: ... cóż, zostałem przyłapany :). Ale <a 
href="http://jb.rouquier.free.fr/">Jean-Baptiste Rouquier</a> odstawił połowę roboty...
no dobra, w zasadzie to więcej. Hmmm... następne pytanie proszę!</p>
</li>

<li><p><b>P: Czy to nie Ty jesteś jednym z tych czterystu dziwnych ludzi od <a 
href="http://code.google.com/summerofcode.html">Google's Summer of 
code</a>, wybranych spośród 9000 hakerów?</b></p> <p>O:
No tak, to ja. Ale miałem zwyczajnie szczęście: wysłałem 8000 z 9000 wszystkich
zgłoszeń, i jedno z nich zostało wybrane. W następnym roku opublikuję kod źródłowy
do mojego automatycznego generatora zgłoszeń. </p> </li>

