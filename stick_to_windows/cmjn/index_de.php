title: Buch-/Druckindustrie</h2>

<p>Linux-Software hat immer noch sehr experimentelle CMYK-Features. Es wird zwar besser, ist aber f&uuml;r den professionellen Gebrauch immer noch nicht wirklich befriedigend.</p>

<p>W&auml;hrend die meisten B&uuml;cher und Magazine &uuml;ber freie Software vor langer Zeit zu Linux gewechselt sind (und sehen sie nicht gro&szlig;artig aus?!), solltest du vorerst bei Windows bleiben, wenn du in der Druckindustrie arbeitest und viel CMYK-Farben, feine Bilder und spezielle Druckeffekte etc. verwendest. Aber du kannst trotzdem Linux installieren und Windows behalten (siehe Installations-Abschnitt) und so beide je nach deinen Bed&uuml;rfnissen anwenden.</p>

<p>Bedenke, dass du auch einen Bildeditor namens <a href="http://www.kanzelsberger.com/pixel/">Pixel</a> benutzen kannst, der CMYK unterstützt. Jedoch glaube ich trotzdem, dass Linux für die Druckindustrie noch nicht bereit ist.</p>

