title: DTP業界は厳しいよ。</h2>

<p>Linuxのソフトでは、CMYKの扱いがまだ実用段階に達していません。進歩はしてるけど、プロの使用にはまだまだ満足いくところまでいっていないのが実状。</p>

<p>フリーなソフトに関する書籍や雑誌はずいぶん前からほとんどがLinuxに移行してる（けっこう高品質でしょ！）。けど、印刷業界で、CMYKの色分解や微妙な写真加工、特殊印刷などなどを多用するのなら、たぶんまだLinuxには移行しない方が安全でしょうね。でも、Windowsをそのままにしておいて、Linuxをインストールすることもできます（インストールのページを参照）。LinuxとWindowsの両方を、用途に応じて使い分ければいいんだから。</p>

