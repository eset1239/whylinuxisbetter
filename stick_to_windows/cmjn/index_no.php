title: Bok- og trykkeriindustrien.</h2>

<p>CMYK-funksjonene som tilbys i Linux-programvare er fortsatt eksperimentelle. Dette blir stadig bedre, men det er ikke godt nok for profesjonell bruk ennå.</p>

<p>De aller fleste bøker og blad om fri programvare, byttet til Linux for en stund siden. Men hvis du jobber i trykkeriindustrien og bruker mange CMYK-farger, med subtile bilder, spesielle printeeffekter, osv., så bør du holde deg til Windows inntil videre. Men du kan fortsatt installere Linux, og beholde Windows (se installasjons-delen av websiden), og bruke dem begge, avhengig av hva du har bruk for.</p>

<p>Vær oppmerksom på at du kan bruke et bilderedigeringsprogram kalt <a href="http://www.kanzelsberger.com/pixel/">Pixel</a>, som støtter CMYK. Uansett mener jeg fortsatt at Linux er ikke helt klar for trykkeriindustrien ennå.</p>

