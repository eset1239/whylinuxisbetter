title: Knjige i štamparska industrija</h2>

<p>Linux softver još uvek ima eksperimentalne CMYK mogućnosti. Ovo se poboljšava, ali još  
nije na nivou kojim bi profesionalci bili zadovoljni.</p>

<p>Dok je mnogo knjiga i magazina koji se bave slobobodnim softvermo prešlo na Linux
pre dosta vremena (i zar ne izgledaju super ?!), ako radite u štamparskoj
industriji i koristite puno CMYK boje, sa rafiniranim slikama, 
specialnim efektima pri štampi itd., verovatno bi trebalo da ostanete na windows-u
za sad. Ali ipak možete instalirati Linux, zadržati Windows (pogledajte sekciju o instalaciji),
i koristiti oba sistema, u zavisnosti od potrebe.</p>

