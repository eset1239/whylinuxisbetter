title: In ấn và xuât bản sách</h2>

<p>Các chương trình trên Linux vẫn còn hỗ trợ CMYK kém. Điều này đang được cải thiện 
nhưng vẫn chưa đủ tốt cho các nhà thiết kế đồ hoạ chuyên nghiệp.</p>

<p>Mặc dù hầu hết sách báo về phần mềm mã nguồn mở chuyển sang Linux từ lâu, nếu 
bạn làm việc in ấn và sử dụng rất nhiều màu CMYK. Bạn nên dùng Windows. Tuy nhiên 
bạn có thể thử Linux, giữ lại Windows (xem mục Cài Đặt).</p>

