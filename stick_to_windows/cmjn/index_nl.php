title: Boek- & printsector</h2>

<p>Linux-software heeft nog steeds zeer experimentele CMYK-mogelijkheden. Dit wordt steeds beter, maar het is nog niet voldoende voor professioneel gebruik.</p>

<p>Hoewel de meeste boeken en tijdschriften over vrije software reeds lang overgestapt zijn naar Linux (zien ze er niet goed uit?), kunt u beter bij Windows blijven als u werkt in de printsector en gebruikmaakt van veel CMYK-kleuren, met subtiele afbeeldingen en speciale geprinte effecten. U kan nog steeds Linux installeren en toch Windows behouden (zie de Installeer Linux-pagina). Afhankelijk van uw noden kan u dan kiezen om op te starten in Linux of Windows.</p>

<p>Merk op dat u ook de image editor <a href="http://www.kanzelsberger.com/pixel/">Pixel</a>, kunt gebruiken, die CMYK-kleuren ondersteunt. Desondanks geloof ik dat Linux nog niet klaar is voor de boek- en printsector.</p>

