title: Industrio de presado de libroj</h2>

<p>La programoj de Linux havas eksperimentajn karakterizaĵojn CMYK. Ĉi tio pliboniĝas, sed, vere, tio ankoraŭ ne estas kontentiga por profesia uzado.</p>

<p>Dum la plejmultaj libroj kaj gazetoj pri libera programaro ŝanĝis al Linux antaŭ tempo (kaj ĉu ili ne aspektas belaj?), se vi laboras en la industrio de presado kaj vi uzas multajn kolorojn CMYK, subtilajn bildojn, specialajn presajn efektojn, ktp., vi eble devus resti kun Windows, dume. Sed vi ankoraŭ povas instali Linux tenante Windows (vidi la sekcion pri instalo) kaj uzi ambaŭ, tio dependas de viaj necesoj.</p>

