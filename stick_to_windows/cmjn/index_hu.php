title: Nyomdaipar</h2>

<p>Linuxos szoftvereken még mindig nagyon kezdetleges a CMYK színrendszer támogatása. Folyamatos a javulás, de még mindig nem kielégítőek az eredmények a professzionális felhasználók számára.</p>

<p>A legtöbb szabad szoftverekről szóló könyv és újság már réges-rég Linuxra váltott (és nem remekül néznek ki?!), ha a nyomdaiparban dolgozol és sok CMYK színt használsz, aprólékos képszerkesztésre, különleges nyomtatási effektekre van szükséged stb., valószínűleg a Windows-hoz vagy láncolva egyelőre. De még mindig telepíthetsz Linuxot, megtartva a Windows-t is (lásd a telepítésről szóló részt), és használhatod mindkettőt a szükségleteidnek megfelelően.</p>

<p>És ne feledjük, használhatsz egy <a href="http://www.kanzelsberger.com/pixel/">Pixel</a> nevű képszerkesztőt is, ami támogatja a CMYK színrendszert. Azonban úgy vélem, a Linux még mindig nem áll teljesen készen a nyomdaiparra.</p>

