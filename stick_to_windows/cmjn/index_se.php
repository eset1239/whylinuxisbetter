title: Bok- & Tryckindustrin</h2>

<p>Mjukvara Linux har fortfarande väldigt experimentela CMYK-funktioner. Detta blir bättre, men det är än inte till förnöjdhet för den professionella marknaden.</p>

<p>Medans de flesta böcker och tidningar om fri mjukvara bytte till Linux för ett bra tag sedan (och ser de inte umerbart fina ut?), om du arbetar i tryckindustrin och använder mycket CMYK-färger med subtila bilder, tryckeffekter, etc, borde du antagligen vara kvar vid Windows än så länge. Men du kan fortfarande installera Linux och behålla Windows (se sektionen ”installation”), och använda de båda, beroende på behov.</p>

<p>Notera att du också kan använda bildredigeringsverktyget
<a href="http://www.kanzelsberger.com/pixel/">Pixel</a>, som stödjer CMYK.
CMYK. Jag tror trots allt att Linux inte är redo för tryckindustrin. Än.</p>

