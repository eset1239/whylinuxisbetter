title: Lavori nell'industria di libri/stampa</h2>

<p>Il software Linux ha ancora caratteristiche CMYK sperimentali. Ciò sta migliorando, 
ma non è ancora soddisfacente per l'uso professionale.</p>

<p>Mentre molti libri e riviste su software libero sono passate a Linux 
molto tempo fa (e non sembrano grandiose?), se lavori nell'industria di 
stampa e usi un sacco di colori CMYK, con immagini fini, effetti di stampa 
speciali, ecc., dovresti rimanere su Windows per adesso. Ma puoi comunque 
installare Linux, tenere Windows (vedi la sezione dell'installazione) ed 
usare entrambi, dipendentemente da ciò che ti serve.</p>

<p>Ricorda che puoi anche usare un editor di immagini chiamato <a href="http://www.kanzelsberger.com/pixel/">Pixel</a>, che supporta CMYK. Tuttavia, credo che Linux non sia ancora pronto per lavori professionali nell'industria della stampa.</p>

