title: 你在出版業或印刷業工作</h2>

<p>Linux 軟體對於 CMYK 的各項特性，仍然處於非常實驗性的階段。它正漸漸地進步，但是目前仍無法滿足專業的需求。</p>

<p>很久以前，許多關於自由軟體的書籍和雜誌便改用 Linux 來印刷出版。（而且看起來很不錯吧？）如果你在印刷業工作，同時在處理細緻的影像以及特殊的印刷效果上，使用了大量的 CMYK 色彩，那麼目前你也許應該繼續使用 Windows。但是你還是可以安裝 Linux 並且保留 Windows（參見「安裝 Linux（並且仍然保留 Windows）」），並且依據你的需求來使用它們兩者。</p>

