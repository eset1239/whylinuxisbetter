title: Tipografia</h2>
<p>Software-ul Linux încă are abilitaţi foarte experimentale CMYK. Acest lucru e îmbunătăţit, dar
încă nu e gata pentru uz profesional.</p>

<p>În timp ce multe cărţi şi reviste despre software gratuit au ales Linux
cu mult timp în urmă (şi arată super!), dacă lucraţi în tipografie
şi folosiţi multe culori CMYK, cu imagini subtile, efecte speciale de imprimare,
etc., probabil ar trebui să rămâneţi la Windows momentan. Dar puteţi totuşi instala
Linux şi păstra Windows (vedeţi secţiunea de instalare) folosindu-le pe ambele.</p>


