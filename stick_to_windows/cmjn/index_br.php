

<h2>Livros & Indústria Gráfica</h2>

<p>O Linux ainda tem muitos recursos CMYK experimentais. Isso está melhorando, mas ainda não está satisfazendo para o uso profissional.</p>

<p>Enquanto muitos livros e revistas sobre software livre mudaram para o Linux há muito tempo (e eles não são ótimos ?!), se você trabalha na indústria gráfica e usa muitas cores CMYK, com imagens delicadas, efeitos especiais de impressão, etc., você provavelmente deve permanecer com o Windows no momento. Mas você ainda pode instalar o Linux, manter o Windows (veja a seção de instalação), e usar os dois, dependendo das suas necessidades.</p>

