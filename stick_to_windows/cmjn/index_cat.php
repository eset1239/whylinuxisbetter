<h2>Els llibres i la indústria de la impressió</h2>

<p>El programari de Linux encara té característiques CMYK molt experimentals. Això està millorant, però no són encara realment satisfactòries per als professionals fer-les servir.</p>

<p>Mentre que la majoria de llibres i revistes sobre programari lliure es van canviar a Linux fa molt de temps (i no semblen genials?!), si treballes a la indústria de la impressió i fas servir un munt de colors CMYK, amb imatges subtils, efectes d'impressió especials, etc., probablement hauràs de continuar en Windows de moment. Però encara pots instal·lar Linux, mantenir Windows (mira la secció de la instal·lació), i usar els dos a la vegada, depenent de les teves necessitats.</p>

