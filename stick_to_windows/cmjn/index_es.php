title: Industria de impresión de libros</h2>

<p>Los programas de Linux tienen características CMYK experimentales. Esto está mejorando; pero, realmente, todavía no es satisfactorio para uso profesional.</p>

<p>Si bien es cierto que la mayoría de libros y revistas sobre software libre cambiaron a Linux hace tiempo (¿y no se ven fenomenal?). Si trabajas en la industria de impresión y usas un montón de colores CMYK, imágenes delicadas, efectos especiales de impresión, etc., mejor deberías quedarte con Windows, por ahora. Pero todavía puedes instalar Linux manteniendo Windows (ver la sección de instalación) y usar ambos, dependiendo de tus necesidades.</p>

