title: Spelletjes</h2>

<p>De meeste spelletjes werken met Windows en niets anders. Sommige hebben Mac-versies en sommige daarvan hebben Linux-versies (Quake 4, Neverwinter Nights, enz.), maar de meeste werken enkel op Windows.</p>

<p>Dus als u veel tijd spendeert met het spelen van nieuwe spelletjes, blijft u best bij Windows. Maar u kan wel nog steeds Linux installeren naast Windows (zie de Installeer Linux-pagina) en beide gebruiken, afhankelijk van uw noden.</p>

<p>Als u graag spelletjes speelt, maar niet veeleisend bent in hun nieuwheid, bemerk dan dat software zoals Wine, CrossOver en PlayOnLinux u kan helpen bij het draaien van niet-zo-recente Windows-spelletjes onder Linux zonder problemen.</p>

