title: Jocs</h2>

<p>La majoria de jocs són compatibles amb Windows i amb cap més. Alguns d'ells tenen versions per a Mac i alguns d'ells versions per a Linux (Quake 4, Neverwinter Nights, etc.), però la majoria d'ells sols es poden executar en Windows.</p>

<p>Així que si passes molt de temps jugant amb jocs moderns, has de continuar en Windows. Però encara pots instal·lar Linux, mantenir Windows (mira la secció de la instal·lació), i usar els dos a la vegada, depenent de les teves necessitats.</p>

