

<h2>Ludoj</h2>

<p>Multaj ludoj estas kongruaj kun Windows, ekskluzive. Kelkaj el ili havas versiojn por Mac kaj kelkaj havas versiojn por Linux (Quake 4, Neverwinter nights, ktp.), sed la plejmultoj nur funkcias en Windows.</p>

<p>Do, se vi elspezas multan tempon ludante novajn ludojn, vi devus resti kun Windows. Sed vi ankoraŭ povas instali Linux tenante Windows (vidi la sekcion pri instalo), kaj uzi ambaŭ, tio dependas de viaj necesoj.</p>

<p>Se vi ŝatas ludi ludojn sed vi ne zorgas pri la freŝo, sciu ke programoj kiel Wine aŭ Cedega povas helpi vin ludi ne-tiom-freŝajn ludojn por Windows sub Linux sen ia problemo.</p>

