title: Игри</h2>

<p>Повечето игри са съвместими единствено с Windows и нищо друго. Някои имат Mac версии, някои Линукс версии (Quake 4, 
Neverwinter Nights и др.), но повечето вървят само на Windows.</p>

<p>Така че, ако прекарвате доста време играейки на последните игри, трябва да се придържате към Windows. Но и така може да си инсталирате Линукс, запазвайки Windows (вижте секцията Инсталиране) и според нуждите си да ги ползвате и двете.</p>

