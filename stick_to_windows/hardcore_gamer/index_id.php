title: Games</h2>

<p>Kebanyakan games memiliki kompatibilitas dengan Windows, dan tidak selainnya. Beberapa games tersebut ada yang diproduksi dalam versi Mac, ada pula yang memiliki versi Linux seperti Quake 4, Neverwinter Nights, dll. Tetapi hampir semua games yang ada saat ini hanya berjalan pada Windows. </p>

<p>Jadi bila kamu menghabiskan banyak waktu untuk memainkan games terbaru, mungkin sebaiknya kamu masih tetap berada di Windows. Tetapi kamu masih dapat menginstall Linux,
buatlah Linux berdampingan dengan Windows (lihat caranya pada bagian instalasi), lalu gunakanlah keduanya sesuai dengan kebutuhan.
</p>

