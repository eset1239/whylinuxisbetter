

<h2>Játékok</h2>

<p>A legtöbb játék csak Windows-on fut, semmi máson. Párnak van Mac-es verziója is, néhánynak linuxos (Quake 4, Neverwinter Nights stb.), de a legtöbb csak Windows-on megy.</p>

<p>Szóval ha sok időt töltesz a legújabb számítógépes játékokkal szórakozva, a Windows-hoz vagy kötve. De még mindig telepíthetsz Linuxot a Windows-t megtartva (lásd a telepítésről szóló részt), így mindkettőt használhatod a szükségleteidnek megfelelően.</p>

<p>Ha szeretsz játszani, de nem ragaszkodsz hozzá, hogy a legújabb legyen, ne feledd, hogy az olyan szoftverek, mint a Wine vagy a Cedega segíthetnek futtatni a nem-épp-a-legújabb windows-os játékokat Linuxon.</p>

