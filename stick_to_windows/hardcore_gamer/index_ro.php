title: Jocurile</h2>

<p>Majoritatea jocurilor sunt compatibile cu Windows, şi nimic altceva.
Unele au versiuni pentru Mac, iar unele au versiuni pentru Linux (Quake 4,
Neverwinter Nights, etc.), dar majoritatea rulează doar pe Windows.</p>

<p>Deşi jocurile pot fi emulate folosind Wine, nu merg toate. Deci dacă
pierdeţi mult timp jucându-vă jocuri noi, ar trebui să rămâneţi la Windows.
Dar puteţi instala Linux, păstra Windows (vedeţi secţiunea de instalare),
şi să le folosiţi pe ambele.</p>

