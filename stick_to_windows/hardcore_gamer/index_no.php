title: Spill</h2>

<p>De fleste spill er laget kun for Windows. Noen spill har Mac-versjoner, og noen har Linux-versjoner (Quake 4, Neverwinter Nights, osv.), men de fleste kjører kun på Windows.</p>

<p>Hvis du bruker mye tid på å spille de siste spillene, bør du fortsette å bruke Windows. Men du kan uansett installere Linux, og beholde Windows (se installasjonsdelen av websiden), og bruke begge systemene, avhengig av hva du har bruk for.</p>

<p>Hvis du spiller spill som ikke er særlig krevende, så kan programvare som Wine og Cedega hjelpe deg å kjøre ikke-fullt-så-nye spill på Linux uten problemer.</p>

