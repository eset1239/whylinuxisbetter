title: Xogos</h2>

<p>A maioría de xogos son compatibles con Windows, e nada máis. Algúns deles teñen versións para Mac e outros teñen versións para Linux (Quake 4, Neverwinter Nights, etc.) pero a maioría funciona só en Windows.</p>

<p>Entón se pasas bastante tempo con xogos recentes, deberías quedarte con Windows. Pero aínda podes instalar Linux mantendo Windows (ver a sección de instalación), e usar ambos, dependendo das túas necesidades.</p>

