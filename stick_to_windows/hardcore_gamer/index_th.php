

<h2>เกมส์</h2>

<p>เกมส์ส่วนมากแล้วมักจะเข้าขากับวินโดวส์อย่างเดียว บางเกมส์อาจมีในรูปแบบของแม็คบ้าง และบางทีก็โผล่มาในรูปแบบของลินุกซ์ (Quake 4, Neverwinter Nights ฯลฯ) แต่ส่วนใหญ่แล้วก็จะอยู่บนวินโดวส์</p>

<p>ดังนั้นหากคุณทุ่มเทเวลาส่วนใหญ่อยู่กับเกมส์ทุกเช้าค่ำ คุณก็ควรจะยึดวินโดวส์ไว้ให้มั่น แต่คุณก็ยังลงลินุกซ์ได้ด้วยนี่หน่า เก็บวินโดวส์ไว้ (ดูรายละเอียดเรื่องการติดตั้ง) และใช้งานมันทั้งคู่ซะเลย</p>

