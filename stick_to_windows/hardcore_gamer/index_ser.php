title: Igre</h2>

<p>Većina igara je kompatibilna sa Windows-om, i ničim drugim. Neke imaju
Mac verzije, a neke imaju i Linux verzije (Quake 4, 
Neverwinter Nights, itd.), ali većina ipak ide samo na Windowsu.</p>

<p>Dakle, ako trošite mnogo vremena igrajući igre, treba da ostanete
na Windows-u. Ali ipak možete instalirati Linux, zadržati Windows
(pogledajte sekciju o instalaciji), i koristiti oba sistema, u zavisnosti od potrebe.</p>

