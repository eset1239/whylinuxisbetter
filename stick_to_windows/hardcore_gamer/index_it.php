<h2>Sei un hardcore gamer</h2>

<p>Molti giochi sono compatibili con Windows e nient'altro. Alcuni 
hanno una versione Mac e altri una versione Linux (Quake 4, Neverwinter 
Nights, ecc.), ma la maggior parte funziona solo su Windows.</p>

<p>Quindi se spendi molto del tuo tempo a giocare a giochi recenti, 
dovresti rimanere su Windows. Ma puoi comunque installare Linux, 
tenere Windows (vedi la sezione dell'installazione) ed usare entrambi, 
dipendentemente da ciò che ti serve.</p>

<p>Se ti piace giocare a videogiochi e non ti interessa molto che siano recenti, sappi che alcuni software come Wine o Cedega possono aiutarti ad avviare giochi non-così-recenti di Windows su Linux senza alcun problema.</p>

