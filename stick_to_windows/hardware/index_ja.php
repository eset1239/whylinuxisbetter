title: ごめん、うちのパソコンじゃ動かないの。</h2>

<p>たいていのハードウェアはLinuxできちんと動きます。けど、最新のハードウェア（開発から数ヶ月とか）はサポート外の場合も。ソフト会社もそうだけど、たいていのハードウェア会社もLinux市場は無視できるほど小さいと思っているから、自分たちでLinux用のドライバを用意しようとは思わない。しかたないからLinuxの開発者がやるんだけど、なかなかいい仕事をするよ。ハードウェアひとつひとつ、きちんと動くようにやってのけるんだから。でも、それなりに時間はかかるよねえ。</p>

<p>自分のハードウェアがサポートされてるかどうかを試すのにいちばん簡単な方法は、ライブCDからLinuxを起動すること。詳しくは「Windowsはそのまんまで、Linuxを試せます。」の記事を読んでね。</p>

