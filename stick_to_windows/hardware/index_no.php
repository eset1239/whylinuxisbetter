title: Hardware</h2>

<p>Det meste av hardware virker på Linux. Men, helt ny hardware (noen få måneder gammle) er noen ganger ikke støttet på Linux. Akkurat som programvareutviklere, anser hardwareutviklere Linux som et lite marked, så de fokuserer ikke på å utgi Linux-drivere for deres hardware. Linux-utviklerne derimot, arbeider hardt for utvikle slike drivere, men det kan fortsatt ta tid før det blir laget.</p>

<p>Den beste og enkleste måten å sjekke ut som din hardware er støttet av Linux, er å kjøre en Linux Live CD (se «Prøv Linux uten forandringer på din datamaskin»-delen av websiden).</p>

