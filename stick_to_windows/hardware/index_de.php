<h2>Hardware</h2>

<p>Die meisten Hardwareteile funktionieren bestens unter Linux. Sehr neue (wenige Monate alte) Hardware jedoch wird manchmal noch nicht unterst&uuml;zt. Wie Software-Anbieter sch&auml;tzen viele Hardware-Anbieter den Linux-Markt sehr gering ein und halten es nicht f&uuml;r n&ouml;tig, Linux-Treiber f&uuml;r ihre Produkte anzubieten. Die Linux-Entwickler arbeiten daran und leisten großartige Arbeit, um jedes Hardwareteil sch&ouml;n funktionieren zu lassen, aber das braucht etwas Zeit.</p>

<p>Der beste und unkomplizierteste Weg zu erfahren, ob deine Hardware von Linux unterst&uuml;zt wird, ist, Linux von einer Live-CD laufen zu lassen (siehe Abschnitt „Probier Linux aus ohne Windows anzutasten“).</p>


