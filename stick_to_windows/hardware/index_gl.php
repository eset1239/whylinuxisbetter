title: Compoñentes e Periféricos (Hardware)</h2>

<p>A maioría dos compoñentes e periféricos traballan ben en Linux. Con todo, algunhas veces, se son moi recentes (duns poucos meses de antigüidade) non funcionarán, de momento. Moitos vendedores aínda consideran que o mercado de Linux é moi pequeno e non se molestan en crear controladores para Linux. Os programadores de Linux traballan niso e fan un traballo excelente para que todo vaia como a seda, pero isto leva o seu tempo.</p>

<p>O xeito mellor e máis directo de saber se todos os compoñentes do teu PC e os teus periféricos valen para Linux é facer funcionar Linux desde un Live CD (ver a sección "Proba Linux sen tocar Windows").</p>

