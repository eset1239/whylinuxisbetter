title: Propriëtaire software</h2>

<p>Tot voor kort beschouwden veel software-uitgevers de softwaremarkt voor Linux te klein om er software voor uit te brengen. Hoewel dit aan het veranderen is, draait de meeste propriëtaire software niet op Linux.</p>

<p>Dat gezegd zijnde: er zijn veel opensourceprogramma's die deze "essentiële" propriëtaire programma's kunnen vervangen (zie <a href="/items/warez">deze pagina</a>).</p>

<p>Probeer deze programma's eens: er bestaan vaak Windows-versies van, die u reeds kan gebruiken. Bekijk hun screenshots en functielijst en kijk of deze voldoen aan uw wensen.</p>

<p>Als er een programma is dat u absoluut niet kan missen, is het behouden van Windows waarschijnlijk een goede keuze. U kan natuurlijk altijd beide besturingssystemen gebruiken (vaak "dualbooten" genoemd), waarmee aan uw wensen wordt voldaan.</p>

<p>Er is ook een project "Wine" waarmee u, in sommige gevallen, een bepaald Windows-programma mee kunt draaien op Linux. Op hun website staan lijsten met compatibele software: <a href="http://www.winehq.org">www.wineHQ.org</a>. Een andere optie is het virtualiseren van Windows in Linux, hoewel dat doorgaans ongeschikt is voor het draaien van programma's die hoge prestaties vereisen.</p>

