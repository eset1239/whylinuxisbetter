title: Proprietäre Software</h2>

<p>Bis vor kurzem dachten viele Software-Entwickler, dass Linux ein zu kleiner Markt ist, um dafür Software zu entwickeln. Obwohl sich dies langsam ändert, läuft die meiste proprietäre Software nicht unter Linux.</p>

<p>Es gibt aber auch diverse Open-Source-Programme, die diese „essentiellen“, proprietären ersetzen können (siehe <a href="/items/warez/index_de.php">diesen Abschnitt</a>).</p>

<p>Gib diesen Produkten eine Chance; sie erscheinen oft auch als Windows-Version, die du jetzt sofort benutzen kannst. Schau dir die Screenshots und die Featureliste an und schau, ob sie deinen Bedürfnissen genügen.</p>

<p>Wenn es ein Programm gibt, ohne das du einfach nicht arbeiten kannst, dann ist das Weiterbenutzen von Windows wahrscheinlich eine gute Wahl. Jedoch kannst du auch beide Betriebssysteme installiert haben (oft „Dual-Boot“ genannt), sodass du immer das verwenden kannst, was du gerade brauchst.</p>

<p>Es gibt auch ein Projekt namens „Wine“, das in der Lage ist, ein Windowsprogramm unter Linux laufen zu lassen. Du kannst die Homepage, die auch alle kompatiblen Anwendungen auflistet, unter <a href="http://www.winehq.org">www.wineHQ.org</a> besuchen. Eine andere verfügbare Option ist das Emulieren von Windows selbst in Linux, obwohl dies normalerweise nicht für ressourcenhungrige Applikationen geeignet ist.</p>

