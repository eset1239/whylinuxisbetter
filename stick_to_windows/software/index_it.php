title: C'è del software proprietario di cui non puoi fare a meno</h2>

<p>Fino a poco fa, molti distributori di software consideravano il mercato di software per Linux 
ancora troppo piccolo per preoccuparsi di fornirgli supporto. Anche se questo sta cambiando, molto 
software proprietario non funziona su Linux.</p>

<p>Detto questo, ci sono molti programmi Open Source che possono rimpiazzare quelli proprietari 
"essenziali" (vedi <a href="/items/warez">questa sezione</a>).</p>

<p>Prova questi programmi; di solito esistono anche versioni Windows che puoi usare da subito. Dà 
uno sguardo ai loro screenshot e lista di caratteristiche e vedi se soddisfano i tuoi bisogni.</p>

<p>Se c'è qualche programma senza il quale non puoi lavorare, allora tenersi Windows è probabilmente 
la miglior scelta. Comunque, puoi tenere entrambi i sistemi operativi installati (in dual-boot), che 
possono essere usati dipendentemente da ciò che ti serve.</p>

<p>Esiste anche un progetto chiamato "Wine" che, in alcuni casi, è capace di far funzionare una data 
applicazione di Windows su Linux. Puoi visitare l'homepage di Wine, che include una lista di 
software compatibile su <a href="http://www.winehq.org">www.wineHQ.org</a>. Un'altra opzione valida 
è quella di emulare Windows in Linux, anche se non sarebbe adatto a far girare programmi di alto 
livello.</p>

