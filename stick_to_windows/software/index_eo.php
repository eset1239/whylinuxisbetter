title: Proprieta programaro</h2>

<p>Ĝis nun, la plejmultaj eldonistoj de programaro konsideras ke Linux havas tre malgrandan merkaton por zorgi eldoni programojn por ĝi. Ĉi tio komencas ŝanĝi, sed, nuntempe, la granda parto de la proprieta programaro ne funkcias en Linux.</p>

<p>Ekzistas multe, multe da liber-koda programaro kiu povas, perfekte, anstataŭigi la proprietan (vidu <a href="/items/warez">ĉi tiun sekcion</a>).</p>

<p>Donu ŝancon al ĉi tiuj programoj (ili kelkofe ankaŭ havas version por Windows). Ĵetu rigardon al la ekranbildoj, al la listo pri karakterizaĵoj kaj vidu ĉu vi ŝatas ilin.</p>

<p>Se ekzistas almenaŭ programo sen kiu vi ne povas labori iel, do vi devus resti kun Windows dume. Sed vi ankoraŭ povas instali Linux tenante Windows (vidu la sekcion pri instalo) kaj uzi ambaŭ, tio dependas de viaj necesoj.</p>

<p>Ekzistas multaj manieroj (ekz. la imitilo<a href="http://www.winehq.org">Wine</a>) por funkciigi programojn de Windows sub Linux.</p>

