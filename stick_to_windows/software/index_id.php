<h2>Software Proprietary</h2>

<p>Hingga sekarang, kebanyakan produsen software masih menganggap bahwa pasar software untuk Linux masih terlampau kecil, hal inilah yang 
menghalangi mereka untuk mengeluarkan software versi Linux. Walaupun saat ini sudah mulai ada perubahan, kebanyakan
software propietary belum dapat berjalan di dalam Linux.  </p>

<p>Seperti yang telah dikatakan sebelumya, ada beberapa aplikasi Open Source yang dapat menggantikan salah satu propietary yang esensial 
(<a href="/items/warez">lihat topik ini</a>). </p>

<p>Cobalah salah satu dari program tersebut, bahkan beberapa dari mereka telah mengeluarkan versi untuk Windows, sehingga dapat kamu gunakan sekarang juga. Amati beberapa screenshot dan daftar fiturnya, dan lihatlah apakah mereka telah cukup memenuhi kebutuhanmu.<p>

<p>Jika ada software yang kamu yang benar-benar tidak dapat bekerja tanpanya, maka tetap menggunakan Windows mungkin adalah pilihan yang baik.
Bagaimanapun juga, kamu dapat menginstall kedua sistem operasi tersebut pada komputermu  (istilahnya "dual-booting"), dan keduanya dapat digunakan sesuai dengan keinginanmu.</p>

<p>Ada pula sebuah proyek bernama "Wine", yang dalam beberapa kasus, dapat menjalankan beberapa aplikasi Windows di Linux. Kamu bisa mengunjungi websitenya di <a href="http://www.winehq.org">www.wineHQ.org</a>, pada website tersebut disajikan daftar software Windows yang dapat dijalankan pada Linux menggunakan Wine. Pilihan lain adalah dengan mengemulasikan Windows di dalam Linux, walau cara seperti ini tidak disarankan untuk menjalankan program dengan tuntutan performansi yang tinggi. </p>


