title: Proprietær software.</h2>

<p>Indtil for nyligt anså software-producenterne Linux-markedet for at være for lille til at ville lave Linux-versioner af deres software. Selv om dette er begyndt at ændre sig, så er det stadigvæk forholdsvist lidt proprietært software der er produceret til Linux.</p>

<p>Med det sagt, så er der mange Open Source programmer der kan erstatte disse "essentielle" proprietære programmer (se <a href="/items/warez">denne sektion</a>).</p>

<p>Prøv disse programmer af; de bliver oftet lavet i Windows-versioner, som du kan afprøve lige nu uden at installere Linux. Tage et kig på program- og feature-listen, og se om de tilfredsstiller dine behov.</p>

<p>Hvis der er et stykke Windows-software du bare ikke kan leve foruden, så er det nok en god idé at beholde Windows. Du kan dog også vælge at installere begge systemer (kaldet "dual-booting"), hvorefter du kan bruge de forskellige programmer under deres eget styresystem efter behov.
</p>

<p>Der er også et projekt kaldet "Wine" som, i de fleste tilfælde, er i stand til at køre Windows-applikationer under Linux. Du kan besøge deres hjemmeside, som også inkluderer en liste over kompatible programmer, på <a href="http://www.winehq.org">www.wineHQ.org</a>. En anden mulighed er at emulere Windows under Linux med et virtualisation-program, omend dette nok ikke er hensigtsmæssigt hvis du vil køre visse high-performance programmer.</p>

