title: Aplicacións ou programas privativos (Software privativo)</h2>

<p>Ata agora, a maioría dos editores de programas ou aplicacións consideraba que Linux tiña un mercado moi pequeno como para interesarse nel. Isto está cambiando, pero de momento gran parte do software privativo non funciona en Linux.</p>

<p>Hai moito, moito software de código aberto que pode perfectamente substituír ao software privativo (ver <a href="/items/warez/index_es.php"></a>esta sección).</p>

<p>Dálle unha oportunidade a estes programas (moitas veces tamén teñen unha versión para *Windows). Dálle unha ollada ás capturas de pantalla e á lista de características, a ver se che gustan.</p>

<p>Se falta polo menos un programa sen o cal non poidas traballar de ningún xeito, entón deberías quedarte con Windows polo de agora. Pero aínda asi podes instalar Linux mantendo Windows (ver a sección de instalación), e usar ambos, dependendo das túas necesidades. Tamén existen moitos xeitos (ex o emulador "Wine") de correr programas Windows baixo Linux.</p>

