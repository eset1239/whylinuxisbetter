<h2>Vlasnički softver</h2>

<p>Do nedavno, mnogi izdavači softvera smatrali su da je linux tržište malo da bi se trudili da izdaju softver za njega. Ovakvo razmišljanje se menja, ali ipak, većina vlasničkog softvera se ne vrti na Linux-u.</p>

<p>Sada kada smo to rekli, postoji nekoliko Open Source programa koji mogu sa zamene "esencijalne" vlasničke
(videti <a href="/items/warez">ovde</a>).</p>

<p>Dajte ovim programima šansu: oni često objave Windows verziju, koju možete odmah koristiti. Pogledajte njihove izglede i liste mogućnosti, i da li zadovoljavaju Vaše potrebe.</p>

<p>Ako ima neki program bez kojeg apsolutno ne možete da radite, onda je zadržavanje na Windows-u najbolje rešenje.  Ipak, možete imati oba operativna sistema instalirana (često zvano "dual-booting"), što se može prilagoditi Vašim potrebama.</p>

<p>Postoji, takođe, projekat zvani "Wine" koji, u nekim slučajevima, može da pokrene određene Windows aplikacije na Linux-u.  Možete posetiti njihov sajt, koji ima spisak kompatibilnog softvera, na <a href="http://www.winehq.org">www.wineHQ.org</a>. Postoji i opcija emulacije Windows-a unutar samog Linux-a, ali
ovo nije rešenje pogodno za pokretanje softvera visokih performansi.</p>

