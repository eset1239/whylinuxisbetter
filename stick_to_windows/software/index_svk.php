title: Autorizovaný (zákonom chránený) softvér</h2>

<p>Ešte donedávna mnohí vydavatelia softvéru považovali programový trh pre Linux za veľmi malý, aby sa zaoberali vydaním softvéru pre Linux. Aj keď sa to mení, väčšina autorizovaného softvéru nefunguje pod Linux-om.</p>

<p>Ale už bolo povedané, že existuje viacero slobodných programov, ktoré dokážu nahradiť "základné" autorizované programy (pozrite si <a href="/items/warez">tento článok</a>).</p>

<p>Dajte týmto programom šancu; často sú vydávané aj verzie pre Windows, ktoré si môžete okamžite vyskúšať. Pozrite si, ako vyzerajú a zoznam toho, čo dokážu, a uvidíte, či dokážu splniť vaše potreby</p>

<p>Ak existuje nejaký program, bez ktorého absolútne nemôžete pracovať, potom ponechať si Windows je dobrá voľba. Avšak stále môžete mať nainštalované oba systémy (nazýva sa to "dual-booting"), a to môže vyhovať vašim požiadavkám.
</p>

<p>Existuje aj projekt nazývaný "Wine", ktorý dokáže v niektorých prípadoch spustiť aplikáciu určenú pre Windows pod Linux-om. Môžete navštíviť domovskú stránku projektu, na ktorej je aj zoznam kompatibilného softvéru <a href="http://www.winehq.org">www.wineHQ.org</a>. Ďalšia možnosť je emulovať samotný Windows pod Linux-om, avšak nie je to typicky vhodné pre používanie náročných aplikácií.</p>

