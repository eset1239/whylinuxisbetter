title: ﻿Software proprietário</h2>

<p>Até recentemente, muitas distribuidoras de software consideravam o mercado de programas para Linux como sendo pequeno para lançarem programas para ele. Isso está começando a mudar, apesar da maioria dos softwares proprietários não rodarem no Linux.
</p>

<p>Existem muitos programas de código aberto que podem substituir esses proprietários "essenciais" (veja esta seção).</p>

<p>Experimente um pouco esses programas; muitos deles lançam versão para Windows, que você pode usar agora mesmo. Dê uma olhada em suas screenshots e lista de recursos, e veja se eles satisfazem suas necessidades.
</p>

<p>Se existe um programa que você absolutamente não pode ficar sem, manter o Windows provavelmente é uma boa opção. Porém, você pode ter os dois sistemas operacionais instalados (geralmente chamado de "dual-booting"), que pode ser usado para suprir suas necessidades.</p>

<p>Existe também um projeto chamado "Wine" que, em alguns casos, é capaz de rodar uma aplicação feita para Windows dentro do Linux. Você pode visitar o site deles, que inclui a lista dos programas compatíveis, em www.wineHQ.org. Outra opção disponível é emular o Windows propriamente dito no Linux, apesar de não ser recomendável para rodar programas de alta performance.</p>

