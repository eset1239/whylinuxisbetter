title: Proprietær programvare.</h2>

<p>Inntil nylig anså mange programvareutviklere Linux-markedet for smått til å utgi Linux-versjoner av deres programvare. Selv om dette begynner å snu, så kjører ikke det meste av proprietær programvare på Linux.</p>

<p>Med det sagt, så finnes det «Open source»-programvare som erstatter essensielle funksjoner som de proprietære tilbyr (se <a href="/items/warez/index_no.php">denne seksjonen</a>).</p>

<p>Prøv disse programmene, de utgir også Windows-versjoner som du kan prøve uten å installere Linux. Ta en titt på skjermbildene og funksjonslistene for å se om de tilfredsstiller dine behov.</p>

<p>Hvis det er programvare som du absolutt ikke kan arbeide uten, så er det lurt å beholde Windows. Men du kan installere to operativsystemer på en datamaskin (ofte kalt «dual booting»), som kan være gunstig i et slikt tilfelle.</p>

<p>Det finnes også et prosjekt som heter «Wine», som i mange tilfeller gjør det mulig å kjøre Windows-applikasjoner på Linux. Du kan besøke deres hjemmeside, som inkluderer lister over kompatibel programvare: <a href="http://www.winehq.org">www.WineHQ.org</a>. En annen metode er å emulere Windows innenfor Linux, men dette er vanligvis ikke passende for krevende programmer.</p>

