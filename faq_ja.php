</li>

<li><p><b>Q: GNOMEとUbuntuばっかりじゃない。KDEに恨みでもあるの？　他のディストリビューションは？</b></p>
<p>A: まさか、まさか。KDEのアプリにはお世話になっているし、愛用してますよ（GNOME対KDEの荒らしに使うエネルギーをKDEのアプリをGNOMEに統合することや、GNOMEのアプリをKDEに統合することに使ってもらえるなら、ほんとはそれがいちばんと思うんだけど）。スクリーンショットのほとんどがUbuntuなのは、単に私がUbuntuを使っているからだし、私がUbuntuの開発に参加しているからなんですよ。だからもちろん、もちろん、お気軽に他のスクリーンショットも送ってくださいね。AmaroKの記事を入れておいたのにお気づきかと思いますけど、他にもKDEのアプリ（GNOMEのアプリもね！）でこれを入れたらというのがあったら、どんどん提案してくださーい。</p> </li>

<li><p><b>Q: あんたにLinuxの何がわかってんの？</b></p> 
